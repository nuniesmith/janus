impl CnsServiceBridge {
    /// Create a new CNS service bridge
    pub fn new(config: CnsBridgeConfig) -> Self {
        Self {
            name: "cns_bridge".to_string(),
            connected: false,
            health_tx: None,
            command_rx: None,
            config,
        }
    }

    /// Create health update channel
    pub fn create_health_channel(&mut self) -> mpsc::Receiver<HealthPayload> {
        let (tx, rx) = mpsc::channel(self.config.buffer_size);
        self.health_tx = Some(tx);
        rx
    }

    /// Set command receiver
    pub fn set_command_receiver(&mut self, rx: broadcast::Receiver<SystemCommand>) {
        self.command_rx = Some(rx);
    }

    /// Send health update to CNS
    pub async fn send_health_update(&self, payload: HealthPayload) -> Result<()> {
        if let Some(tx) = &self.health_tx {
            tx.send(payload)
                .await
                .map_err(|e| Error::Other(format!("Failed to send health update: {}", e)))?;
        } else {
            return Err(Error::Other("CNS bridge not connected".to_string()));
        }

        Ok(())
    }

    /// Report region health
    pub async fn report_region_health(
        &self,
        region: &str,
        healthy: bool,
        metrics: HashMap<String, f64>,
    ) -> Result<()> {
        let payload = if healthy {
            HealthPayload::healthy(region)
        } else {
            HealthPayload::unhealthy(region, "Region unhealthy")
        };

        let payload = HealthPayload { metrics, ..payload };

        self.send_health_update(payload).await
    }
}

