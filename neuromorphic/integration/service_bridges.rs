//! Service Bridges Module
//!
//! Part of Integration region - Connecting neuromorphic architecture to services
//!
//! This module provides bridges that connect the neuromorphic brain regions
//! to the JANUS service layer:
//! - Forward Service: Real-time signal generation and streaming
//! - Backward Service: Historical analysis, persistence, and training
//! - CNS Service: Health monitoring, metrics, and auto-recovery
//! - Data Service: Market data ingestion and distribution
//!
//! ## Architecture
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────────┐
//! │                    NEUROMORPHIC CORE                             │
//! │  ┌─────────┐  ┌─────────┐  ┌─────────┐  ┌─────────┐           │
//! │  │ Cortex  │  │Cerebellum│  │Thalamus │  │Amygdala │           │
//! │  └────┬────┘  └────┬────┘  └────┬────┘  └────┬────┘           │
//! │       │            │            │            │                  │
//! │       └────────────┴──────┬─────┴────────────┘                  │
//! │                           │                                      │
//! │                  ┌────────▼────────┐                            │
//! │                  │ Service Bridges │                            │
//! │                  └────────┬────────┘                            │
//! └──────────────────────────┬─────────────────────────────────────┘
//!                            │
//!         ┌──────────────────┼──────────────────┐
//!         │                  │                  │
//!    ┌────▼────┐       ┌─────▼────┐      ┌─────▼────┐
//!    │ Forward │       │ Backward │      │   CNS    │
//!    │ Service │       │ Service  │      │ Service  │
//!    └─────────┘       └──────────┘      └──────────┘
//! ```

