/// Service bridge trait - implemented by each service bridge
#[async_trait]
pub trait ServiceBridge: Send + Sync {
    /// Get bridge name
    fn name(&self) -> &str;

    /// Check if bridge is connected
    fn is_connected(&self) -> bool;

    /// Connect to the service
    async fn connect(&mut self) -> Result<()>;

    /// Disconnect from the service
    async fn disconnect(&mut self) -> Result<()>;

    /// Send a message to the service
    async fn send(&self, message: BridgeMessage) -> Result<()>;

    /// Health check
    async fn health_check(&self) -> bool;
}

/// Forward Service Bridge
/// Sends signals from neuromorphic processing to forward service for execution
pub struct ForwardServiceBridge {
    name: String,
    connected: bool,
    signal_tx: Option<mpsc::Sender<NeuromorphicSignal>>,
    config: ForwardBridgeConfig,
}

/// Configuration for forward service bridge
#[derive(Debug, Clone)]
pub struct ForwardBridgeConfig {
    /// Minimum confidence to forward signals
    pub min_confidence: f64,

    /// Maximum risk level to forward
    pub max_risk_level: RiskLevel,

    /// Enable signal filtering
    pub enable_filtering: bool,

    /// Buffer size for signal channel
    pub buffer_size: usize,
}

impl Default for ForwardBridgeConfig {
    fn default() -> Self {
        Self {
            min_confidence: 0.6,
            max_risk_level: RiskLevel::High,
            enable_filtering: true,
            buffer_size: 1000,
        }
    }
}

impl ForwardServiceBridge {
    /// Create a new forward service bridge
    pub fn new(config: ForwardBridgeConfig) -> Self {
        Self {
            name: "forward_bridge".to_string(),
            connected: false,
            signal_tx: None,
            config,
        }
    }

    /// Get signal receiver (for forward service to consume)
    pub fn create_signal_channel(&mut self) -> mpsc::Receiver<NeuromorphicSignal> {
        let (tx, rx) = mpsc::channel(self.config.buffer_size);
        self.signal_tx = Some(tx);
        rx
    }

    /// Send a signal to forward service
    pub async fn send_signal(&self, signal: NeuromorphicSignal) -> Result<()> {
        // Apply filtering if enabled
        if self.config.enable_filtering {
            if !signal.should_execute(self.config.min_confidence, self.config.max_risk_level) {
                debug!(
                    "Signal filtered: confidence={}, risk={:?}",
                    signal.confidence, signal.risk_level
                );
                return Ok(());
            }
        }

        if let Some(tx) = &self.signal_tx {
            tx.send(signal)
                .await
                .map_err(|e| Error::Other(format!("Failed to send signal: {}", e)))?;
        } else {
            return Err(Error::Other("Forward bridge not connected".to_string()));
        }

        Ok(())
    }
}

#[async_trait]
impl ServiceBridge for ForwardServiceBridge {
    fn name(&self) -> &str {
        &self.name
    }

    fn is_connected(&self) -> bool {
        self.connected && self.signal_tx.is_some()
    }

    async fn connect(&mut self) -> Result<()> {
        info!("Connecting forward service bridge...");
        // Create a dummy channel for testing if not already set
        if self.signal_tx.is_none() {
            let (tx, _rx) = tokio::sync::mpsc::channel(100);
            self.signal_tx = Some(tx);
        }
        self.connected = true;
        Ok(())
    }

    async fn disconnect(&mut self) -> Result<()> {
        info!("Disconnecting forward service bridge...");
        self.connected = false;
        self.signal_tx = None;
        Ok(())
    }

    async fn send(&self, message: BridgeMessage) -> Result<()> {
        match message {
            BridgeMessage::Signal(signal) => self.send_signal(signal).await,
            _ => {
                warn!("Forward bridge received non-signal message");
                Ok(())
            }
        }
    }

    async fn health_check(&self) -> bool {
        self.is_connected()
    }
}

/// Backward Service Bridge
/// Sends training data and receives historical analysis from backward service
