/// Message types for inter-service communication
#[derive(Debug, Clone, Serialize, Deserialize)]
pub enum BridgeMessage {
    /// Signal generated by neuromorphic processing
    Signal(NeuromorphicSignal),

    /// Training data for backward service
    TrainingData(TrainingPayload),

    /// Health/metrics update for CNS
    HealthUpdate(HealthPayload),

    /// Market data from data service
    MarketData(MarketDataPayload),

    /// Configuration update
    ConfigUpdate(ConfigPayload),

    /// System command
    Command(SystemCommand),
}

/// Signal generated by neuromorphic processing
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct NeuromorphicSignal {
    /// Unique signal ID
    pub id: String,

    /// Trading symbol
    pub symbol: String,

    /// Signal type (buy, sell, hold)
    pub signal_type: SignalType,

    /// Confidence score (0.0 to 1.0)
    pub confidence: f64,

    /// Source brain region(s)
    pub source_regions: Vec<String>,

    /// Timestamp
    pub timestamp: DateTime<Utc>,

    /// Risk assessment from amygdala
    pub risk_level: RiskLevel,

    /// Recommended position size from hypothalamus
    pub position_size: Option<f64>,

    /// Stop loss from cerebellum
    pub stop_loss: Option<f64>,

    /// Take profit from cerebellum
    pub take_profit: Option<f64>,

    /// Supporting indicators
    pub indicators: HashMap<String, f64>,

    /// Fear/greed index from amygdala
    pub fear_index: f64,

    /// Reasoning from prefrontal cortex
    pub reasoning: Option<String>,
}

impl NeuromorphicSignal {
    /// Create a new signal
    pub fn new(symbol: String, signal_type: SignalType, confidence: f64) -> Self {
        Self {
            id: uuid::Uuid::new_v4().to_string(),
            symbol,
            signal_type,
            confidence,
            source_regions: Vec::new(),
            timestamp: Utc::now(),
            risk_level: RiskLevel::Medium,
            position_size: None,
            stop_loss: None,
            take_profit: None,
            indicators: HashMap::new(),
            fear_index: 0.5,
            reasoning: None,
        }
    }

    /// Check if signal should be executed
    pub fn should_execute(&self, min_confidence: f64, max_risk: RiskLevel) -> bool {
        self.confidence >= min_confidence && self.risk_level <= max_risk
    }

    /// Add source region
    pub fn add_source(mut self, region: &str) -> Self {
        self.source_regions.push(region.to_string());
        self
    }

    /// Set risk assessment
    pub fn with_risk(mut self, risk: RiskLevel, fear_index: f64) -> Self {
        self.risk_level = risk;
        self.fear_index = fear_index;
        self
    }

    /// Set position sizing
    pub fn with_position(mut self, size: f64, stop_loss: f64, take_profit: f64) -> Self {
        self.position_size = Some(size);
        self.stop_loss = Some(stop_loss);
        self.take_profit = Some(take_profit);
        self
    }
}

