        assert!(system.is_running().await);

        // Trigger emergency halt
        system
            .trigger_emergency_halt("Test halt".to_string(), 0.95)
            .await
            .unwrap();

        let state = system.get_state().await;
        assert!(state.emergency_halt);
        assert_eq!(
            state.halt_reason,
            Some("Test halt (score: 0.95)".to_string())
        );

        // Reset
        system.reset_emergency_halt().await.unwrap();
        let state = system.get_state().await;
        assert!(!state.emergency_halt);
