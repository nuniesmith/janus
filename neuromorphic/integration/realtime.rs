//! Real-time Trading Integration
//!
//! Integrates the cognitive core with the live trading engine.
//! Provides real-time market data processing, fear detection, and decision support.
//!
//! # Architecture
//!
//! ```text
//! ┌─────────────────────────────────────────────────────────────┐
//! │                  Real-time Integration                       │
//! ├─────────────────────────────────────────────────────────────┤
//! │                                                               │
//! │  ┌──────────────┐    ┌──────────────┐    ┌──────────────┐  │
//! │  │Market Data   │───►│  Cognitive   │───►│  Trading     │  │
//! │  │  Stream      │    │    Core      │    │  Decisions   │  │
//! │  └──────────────┘    └──────────────┘    └──────────────┘  │
//! │         │                    │                    │          │
//! │         │                    ▼                    │          │
//! │         │            ┌──────────────┐            │          │
//! │         │            │Fear Network  │            │          │
//! │         │            │  Detection   │            │          │
//! │         │            └──────────────┘            │          │
//! │         │                    │                    │          │
//! │         └────────────────────┴────────────────────┘          │
//! │                              │                                │
//! │                              ▼                                │
//! │                      ┌──────────────┐                        │
//! │                      │  Execution   │                        │
//! │                      │   Engine     │                        │
//! │                      └──────────────┘                        │
//! │                                                               │
//! └─────────────────────────────────────────────────────────────┘
//! ```

