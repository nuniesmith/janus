    #[test]
    fn test_bullish_surge_detection() {
        let mut strategy = MomentumSurgeStrategy::new(MomentumSurgeConfig {
            lookback_candles: 10,
            momentum_threshold_pct: 5.0,
            volume_spike_multiplier: 2.0,
            pullback_threshold: 0.003,
            min_confidence: 0.40,
            warmup_candles: 15,
            ..Default::default()
        });

        // Warmup with stable prices and volume
        for i in 0..15 {
            let price = 1000.0 + (i as f64) * 0.1;
            strategy.update_ohlcv(price - 1.0, price + 2.0, price - 2.0, price, 100.0);
        }

        assert!(strategy.is_ready());

        // Now spike the price up 10% over 5 candles
        for i in 0..5 {
            let base = 1001.5 + (i as f64) * 22.0;
            strategy.update_ohlcv(
                base - 5.0,
                base + 10.0,
                base - 10.0,
                base,
                500.0, // 5x normal volume
            );
        }

        // After the spike, add a small pullback candle
        let peak = 1001.5 + 4.0 * 22.0; // ~1089.5
        let pullback_price = peak * 0.994; // pulled back slightly
        let result = strategy.update_ohlcv(
            pullback_price - 3.0, // open below close = bullish
            pullback_price + 5.0,
            pullback_price - 5.0,
            pullback_price, // bullish close above open
            600.0,          // still elevated volume
        );

        // We should see either Buy or Hold; the key is no panic
        // Exact signal depends on indicator warmup state
        assert!(
            result.signal == MomentumSurgeSignal::Buy || result.signal == MomentumSurgeSignal::Hold
        );
        assert!(!result.reason.is_empty());
    }

