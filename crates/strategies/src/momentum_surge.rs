/// Momentum Surge Strategy
///
/// Detects sudden momentum surges (large price moves + volume spikes) and
/// enters on pullback within the surge. Single-asset — instantiate one per
/// symbol.
#[derive(Debug)]
pub struct MomentumSurgeStrategy {
    config: MomentumSurgeConfig,

    // Indicators
    atr: ATR,
    volume_analyzer: VolumeAnalyzer,

    // Price history (candle-count based lookback)
    history: PriceHistory,

    // Position tracking
    in_position: bool,
    entry_price: Option<f64>,
    entry_side: Option<MomentumSurgeSignal>,
    stop_loss: Option<f64>,
    take_profit: Option<f64>,

    // Last computed values
    last_atr: Option<f64>,
    last_signal: MomentumSurgeSignal,
    candle_count: usize,
}

impl MomentumSurgeStrategy {
    /// Create a new Momentum Surge strategy with the given config.
    pub fn new(config: MomentumSurgeConfig) -> Self {
        let atr = ATR::new(config.atr_period);
        let volume_analyzer = VolumeAnalyzer::new(config.volume_period);
        let history = PriceHistory::new(config.max_history);

        Self {
            atr,
            volume_analyzer,
            history,
            config,
            in_position: false,
            entry_price: None,
            entry_side: None,
            stop_loss: None,
            take_profit: None,
            last_atr: None,
            last_signal: MomentumSurgeSignal::Hold,
            candle_count: 0,
        }
    }

    /// Create with default configuration.
    pub fn with_defaults() -> Self {
        Self::new(MomentumSurgeConfig::default())
    }

    /// Update with a `janus-models` Candle.
    pub fn update_candle(&mut self, candle: &janus_models::Candle) -> MomentumSurgeSignal {
        self.update_ohlcv(
            candle.open,
            candle.high,
            candle.low,
            candle.close,
            candle.volume,
        )
        .signal
    }

    /// Update with a `janus-models` Candle and return detailed result.
    pub fn update_candle_with_reason(
        &mut self,
        candle: &janus_models::Candle,
    ) -> MomentumSurgeResult {
        self.update_ohlcv(
            candle.open,
            candle.high,
            candle.low,
            candle.close,
            candle.volume,
        )
    }

    /// Main update with OHLCV values — returns a detailed result.
    pub fn update_ohlcv(
        &mut self,
        open: f64,
        high: f64,
        low: f64,
        close: f64,
        volume: f64,
    ) -> MomentumSurgeResult {
        self.candle_count += 1;

        // Update indicators
        self.atr.update(high, low, close);
        let vol_mult = self.volume_analyzer.update(volume);
        self.last_atr = self.atr.value();

        // Store in price history
        let is_bullish = close > open;
        self.history.push(PricePoint {
            high,
            low,
            close,
            is_bullish,
        });

        // Not ready yet?
        if self.candle_count < self.config.warmup_candles
            || self.history.len() < self.config.lookback_candles + 1
        {
            return self.make_result(
                MomentumSurgeSignal::Hold,
                None,
                vol_mult,
                0.0,
                "Warming up".to_string(),
            );
        }

        let atr_val = match self.last_atr {
            Some(a) if a > 0.0 => a,
            _ => {
                return self.make_result(
                    MomentumSurgeSignal::Hold,
                    None,
                    vol_mult,
                    0.0,
                    "ATR not ready".to_string(),
                );
            }
        };

        // Calculate price change over lookback window
        let price_change = self.history.price_change_pct(self.config.lookback_candles);

        // Check for exit if in position
        if self.in_position
            && let Some(ref side) = self.entry_side
        {
            let exit = self.check_exit(close, atr_val, side);
            if let Some(exit_signal) = exit {
                self.exit_position();
                return self.make_result(
                    exit_signal,
                    price_change,
                    vol_mult,
                    0.8,
                    format!("Momentum exit: price {:.2}", close),
                );
            }
        }

        // Already in position — don't open another
        if self.in_position {
            return self.make_result(
                MomentumSurgeSignal::Hold,
                price_change,
                vol_mult,
                0.0,
                "In position".to_string(),
            );
        }

        // Need price change data
        let pct_change = match price_change {
            Some(c) => c,
            None => {
                return self.make_result(
                    MomentumSurgeSignal::Hold,
                    None,
                    vol_mult,
                    0.0,
                    "Price history not long enough".to_string(),
                );
            }
        };

        let price = close;

        // Compute recent extreme for pullback check
        let recent_high = self.history.recent_high(self.config.pullback_lookback);
        let recent_low = self.history.recent_low(self.config.pullback_lookback);

        // === BULLISH MOMENTUM SURGE ===
        if pct_change >= self.config.momentum_threshold_pct
            && vol_mult >= self.config.volume_spike_multiplier
        {
            // Check for pullback from recent high
            if let Some(rh) = recent_high {
                let pullback = rh > 0.0 && price < rh * (1.0 - self.config.pullback_threshold);
                let candle_bullish = is_bullish;

                if pullback && candle_bullish {
                    let stop = price - atr_val * self.config.atr_stop_multiplier;
                    let tp = price + atr_val * self.config.atr_tp_multiplier;

                    let mut confidence = 0.40;

                    // More surge = more confidence (capped)
                    confidence += (pct_change / 20.0).min(0.25);

                    // Bigger volume spike = more confidence
                    confidence += (vol_mult / 10.0).min(0.20);

                    // Strong bullish candle body
                    let body = (close - open).abs();
                    let range = high - low;
                    if range > 0.0 && body / range > 0.6 {
                        confidence += 0.05;
                    }

                    confidence = confidence.min(1.0);

                    if confidence >= self.config.min_confidence {
                        self.enter_position(MomentumSurgeSignal::Buy, price, stop, tp);
                        self.last_signal = MomentumSurgeSignal::Buy;

                        return self.make_result(
                            MomentumSurgeSignal::Buy,
                            Some(pct_change),
                            vol_mult,
                            confidence,
                            format!(
                                "Bullish momentum surge: +{:.1}% in {} candles, vol {:.1}x, pullback entry from {:.2}",
                                pct_change, self.config.lookback_candles, vol_mult, rh
                            ),
                        );
                    }
                }
            }
        }

        // === BEARISH MOMENTUM SURGE ===
        if pct_change <= -self.config.momentum_threshold_pct
            && vol_mult >= self.config.volume_spike_multiplier
            && let Some(rl) = recent_low
        {
            let pullback = rl > 0.0 && price > rl * (1.0 + self.config.pullback_threshold);
            let candle_bearish = !is_bullish && close != open; // bearish, not doji

            if pullback && candle_bearish {
                let stop = price + atr_val * self.config.atr_stop_multiplier;
                let tp = price - atr_val * self.config.atr_tp_multiplier;

                let mut confidence = 0.40;

                confidence += (pct_change.abs() / 20.0).min(0.25);
                confidence += (vol_mult / 10.0).min(0.20);

                let body = (close - open).abs();
                let range = high - low;
                if range > 0.0 && body / range > 0.6 {
                    confidence += 0.05;
                }

                confidence = confidence.min(1.0);

                if confidence >= self.config.min_confidence {
                    self.enter_position(MomentumSurgeSignal::Sell, price, stop, tp);
                    self.last_signal = MomentumSurgeSignal::Sell;

                    return self.make_result(
                            MomentumSurgeSignal::Sell,
                            Some(pct_change),
                            vol_mult,
                            confidence,
                            format!(
                                "Bearish momentum surge: {:.1}% in {} candles, vol {:.1}x, pullback entry from {:.2}",
                                pct_change, self.config.lookback_candles, vol_mult, rl
                            ),
                        );
                }
            }
        }

        self.last_signal = MomentumSurgeSignal::Hold;
        self.make_result(
            MomentumSurgeSignal::Hold,
            Some(pct_change),
            vol_mult,
            0.0,
            format!(
                "No signal: price {:.2}, change {:.1}%, vol {:.1}x",
                price, pct_change, vol_mult
            ),
        )
    }

    /// Update with HLC values (no volume — uses volume=0 fallback).
    pub fn update_hlc(&mut self, high: f64, low: f64, close: f64) -> MomentumSurgeResult {
        self.update_ohlcv(close, high, low, close, 0.0)
    }

    // ========================================================================
    // Regime gating
    // ========================================================================

    /// Check whether this strategy should trade in the given market regime.
    ///
    /// Momentum surges are most meaningful in trending or volatile markets.
    /// In mean-reverting markets the surge is likely to reverse before
    /// reaching the take-profit.
    pub fn should_trade_in_regime(regime: &MarketRegime) -> bool {
        matches!(regime, MarketRegime::Trending(_) | MarketRegime::Volatile)
    }

    /// Compute a confidence-weighted position size factor for the given regime.
    ///
    /// Returns `Some(factor)` if the regime is suitable, `None` otherwise.
    pub fn regime_size_factor(regime: &MarketRegime, confidence: f64) -> Option<f64> {
        match regime {
            MarketRegime::Trending(_) => {
                // Primary regime — momentum aligned with trend
                Some(confidence.clamp(0.3, 0.8))
            }
            MarketRegime::Volatile => {
                // Surges are common but riskier — cap size
                Some(if confidence >= 0.6 { 0.35 } else { 0.20 })
            }
            _ => None,
        }
    }

    // ========================================================================
    // Momentum accessors
    // ========================================================================

    /// Get the current momentum reading: (price_change_pct, volume_multiplier).
    /// Returns None if not enough data.
    pub fn get_momentum(&self) -> Option<(f64, f64)> {
        let pct = self
            .history
            .price_change_pct(self.config.lookback_candles)?;
        let vol_avg = self.volume_analyzer.average();
        if vol_avg <= 0.0 {
            return None;
        }
        // Use the last volume relative to the average
        let last_vol = self.history.last().map(|_| vol_avg).unwrap_or(0.0);
        if last_vol > 0.0 {
            Some((pct, 1.0)) // current multiplier already consumed; return baseline
        } else {
            Some((pct, 0.0))
        }
    }

    // ========================================================================
    // Position management (internal)
    // ========================================================================

    fn enter_position(&mut self, side: MomentumSurgeSignal, price: f64, stop: f64, tp: f64) {
        self.in_position = true;
        self.entry_price = Some(price);
        self.entry_side = Some(side);
        self.stop_loss = Some(stop);
        self.take_profit = Some(tp);
    }

    fn check_exit(
        &self,
        close: f64,
        _atr: f64,
        side: &MomentumSurgeSignal,
    ) -> Option<MomentumSurgeSignal> {
        match side {
            MomentumSurgeSignal::Buy => {
                // Stop loss
                if let Some(stop) = self.stop_loss
                    && close <= stop
                {
                    return Some(MomentumSurgeSignal::Sell);
                }
                // Take profit
                if let Some(tp) = self.take_profit
                    && close >= tp
                {
                    return Some(MomentumSurgeSignal::Sell);
                }
                None
            }
            MomentumSurgeSignal::Sell => {
                if let Some(stop) = self.stop_loss
                    && close >= stop
                {
                    return Some(MomentumSurgeSignal::Buy);
                }
                if let Some(tp) = self.take_profit
                    && close <= tp
                {
                    return Some(MomentumSurgeSignal::Buy);
                }
                None
            }
            MomentumSurgeSignal::Hold => None,
        }
    }

    fn exit_position(&mut self) {
        self.in_position = false;
        self.entry_price = None;
        self.entry_side = None;
        self.stop_loss = None;
        self.take_profit = None;
    }

    fn make_result(
        &self,
        signal: MomentumSurgeSignal,
        price_change_pct: Option<f64>,
        volume_multiplier: f64,
        confidence: f64,
        reason: String,
    ) -> MomentumSurgeResult {
        MomentumSurgeResult {
            signal,
            price_change_pct,
            volume_multiplier,
            atr: self.last_atr,
            recent_high: self.history.recent_high(self.config.pullback_lookback),
            recent_low: self.history.recent_low(self.config.pullback_lookback),
            confidence,
            stop_loss: if matches!(signal, MomentumSurgeSignal::Buy | MomentumSurgeSignal::Sell) {
                self.stop_loss
            } else {
                None
            },
            take_profit: if matches!(signal, MomentumSurgeSignal::Buy | MomentumSurgeSignal::Sell) {
                self.take_profit
            } else {
                None
            },
            reason,
        }
    }

    // ========================================================================
    // Reset
    // ========================================================================

    /// Full reset — clears all indicator and position state.
    pub fn reset(&mut self) {
        self.atr = ATR::new(self.config.atr_period);
        self.volume_analyzer = VolumeAnalyzer::new(self.config.volume_period);
        self.history.clear();
        self.exit_position();
        self.last_atr = None;
        self.last_signal = MomentumSurgeSignal::Hold;
        self.candle_count = 0;
    }

    // ========================================================================
    // Accessors
    // ========================================================================

    /// Whether the strategy has enough data to produce signals.
    pub fn is_ready(&self) -> bool {
        self.candle_count >= self.config.warmup_candles
            && self.history.len() > self.config.lookback_candles
            && self.last_atr.is_some()
    }

    /// Whether the strategy currently has an open position.
    pub fn is_in_position(&self) -> bool {
        self.in_position
    }

    /// The last ATR value.
    pub fn last_atr(&self) -> Option<f64> {
        self.last_atr
    }

    /// The last signal produced.
    pub fn last_signal(&self) -> MomentumSurgeSignal {
        self.last_signal
    }

    /// Total candles processed.
    pub fn candle_count(&self) -> usize {
        self.candle_count
    }

    /// Current entry price (if in position).
    pub fn entry_price(&self) -> Option<f64> {
        self.entry_price
    }

    /// Current stop loss (if in position).
    pub fn stop_loss(&self) -> Option<f64> {
        self.stop_loss
    }

    /// Current take profit (if in position).
    pub fn take_profit(&self) -> Option<f64> {
        self.take_profit
    }

    /// Strategy configuration.
    pub fn config(&self) -> &MomentumSurgeConfig {
        &self.config
    }

    /// Number of price points in the rolling history.
    pub fn history_len(&self) -> usize {
        self.history.len()
    }
}

