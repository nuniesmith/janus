//! # Momentum Surge Strategy
//!
//! Detects sudden momentum surges — large price moves accompanied by volume
//! spikes — and enters on the first pullback within the surge. Uses
//! candle-count based lookback instead of `chrono::DateTime` timestamps,
//! making it compatible with JANUS's tick-aggregated candle pipeline.
//!
//! ## How It Works
//!
//! 1. **Price Change Detection**: Tracks the percentage price change over a
//!    configurable lookback window (in candles, not minutes). A surge is
//!    detected when the change exceeds `momentum_threshold_pct`.
//!
//! 2. **Volume Spike Confirmation**: The current candle's volume must exceed
//!    the rolling average by `volume_spike_multiplier` to confirm real
//!    institutional participation (not just a thin-book wick).
//!
//! 3. **Pullback Entry**: Rather than chasing the top/bottom of the surge,
//!    the strategy waits for a slight pullback from the recent extreme before
//!    entering. This gives a better risk/reward entry point.
//!
//! 4. **ATR-Based Stops**: Stop loss and take profit are placed at ATR
//!    multiples from entry, with tighter stops for the inherently riskier
//!    momentum plays.
//!
//! ## Regime Integration
//!
//! Momentum surges happen in trending and volatile markets:
//!
//! - **Trending(Bullish/Bearish)** → Yes (momentum aligned with trend)
//! - **Volatile** → Yes (surges are common in volatile regimes)
//! - **MeanReverting** → No (surges are likely to revert)
//! - **Uncertain** → No (spurious signals)
//!
//! ## Differences from Kraken Version
//!
//! The Kraken `MomentumSurge` used `chrono::DateTime<Utc>` price history with
//! minute-based lookback (`Duration::minutes(15)`). This JANUS version uses a
//! rolling window of candle close prices, where the lookback is specified in
//! number of candles. This is more natural for the tick-aggregated candle
//! pipeline where candle duration depends on tick rate, not wall-clock time.
//!
//! ## Origin
//!
//! Ported from Kraken's `strategy/intraday.rs::MomentumSurge`, adapted for
//! the JANUS single-asset design, candle-count based lookback, `janus-regime`
//! indicator types, and regime-aware position sizing.

use janus_regime::MarketRegime;
use janus_regime::indicators::ATR;
use serde::{Deserialize, Serialize};

