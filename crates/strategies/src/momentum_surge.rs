/// Signal generated by the Momentum Surge strategy.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum MomentumSurgeSignal {
    /// Enter long — bullish momentum surge with pullback entry.
    Buy,
    /// Enter short / exit long — bearish momentum surge with pullback entry.
    Sell,
    /// No action.
    Hold,
}

impl std::fmt::Display for MomentumSurgeSignal {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            MomentumSurgeSignal::Buy => write!(f, "BUY"),
            MomentumSurgeSignal::Sell => write!(f, "SELL"),
            MomentumSurgeSignal::Hold => write!(f, "HOLD"),
        }
    }
}

