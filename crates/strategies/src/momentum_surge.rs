    #[test]
    fn test_many_candles_no_panic() {
        let mut strategy = MomentumSurgeStrategy::new(MomentumSurgeConfig::crypto_aggressive());
        let mut price = 50000.0;
        for i in 0..2000 {
            let direction = if (i / 80) % 2 == 0 { 1.0 } else { -1.0 };
            price += direction * 25.0 + (i as f64 * 0.1).sin() * 40.0;
            price = price.max(100.0); // keep positive
            let high = price + 30.0;
            let low = price - 30.0;
            let open = price - direction * 5.0;
            let volume = 1000.0 + (i as f64 * 0.3).cos().abs() * 3000.0;
            let _ = strategy.update_ohlcv(open, high, low, price, volume);
        }
        assert_eq!(strategy.candle_count(), 2000);
        // History should be capped at max_history
        assert!(strategy.history_len() <= strategy.config().max_history);
    }

