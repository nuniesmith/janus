/// Rolling volume analyzer for spike detection.
#[derive(Debug)]
struct VolumeAnalyzer {
    period: usize,
    volumes: Vec<f64>,
}

impl VolumeAnalyzer {
    fn new(period: usize) -> Self {
        Self {
            period: period.max(1),
            volumes: Vec::with_capacity(period),
        }
    }

    /// Push a new volume value. Returns the multiplier (volume / rolling avg).
    fn update(&mut self, volume: f64) -> f64 {
        let avg = self.average();

        self.volumes.push(volume);
        if self.volumes.len() > self.period {
            self.volumes.remove(0);
        }

        if avg > 0.0 { volume / avg } else { 1.0 }
    }

    /// Rolling average volume.
    fn average(&self) -> f64 {
        if self.volumes.is_empty() {
            0.0
        } else {
            self.volumes.iter().sum::<f64>() / self.volumes.len() as f64
        }
    }
}

// ============================================================================
// Internal: Price History (candle-count based)
// ============================================================================

/// Minimal candle data stored in the rolling history window.
#[derive(Debug, Clone)]
#[allow(dead_code)]
struct PricePoint {
    high: f64,
    low: f64,
    close: f64,
    is_bullish: bool,
}

/// Rolling price history for lookback calculations.
#[derive(Debug)]
struct PriceHistory {
    points: Vec<PricePoint>,
    max_size: usize,
}

impl PriceHistory {
    fn new(max_size: usize) -> Self {
        Self {
            points: Vec::with_capacity(max_size),
            max_size: max_size.max(1),
        }
    }

    fn push(&mut self, point: PricePoint) {
        self.points.push(point);
        if self.points.len() > self.max_size {
            self.points.remove(0);
        }
    }

    fn len(&self) -> usize {
        self.points.len()
    }

    /// Calculate price change (%) over the last `lookback` candles.
    /// Returns None if not enough history.
    fn price_change_pct(&self, lookback: usize) -> Option<f64> {
        if self.points.len() < lookback + 1 {
            return None;
        }

        let current_idx = self.points.len() - 1;
        let old_idx = current_idx.saturating_sub(lookback);

        let old_price = self.points[old_idx].close;
        let current_price = self.points[current_idx].close;

        if old_price == 0.0 {
            return None;
        }

        Some((current_price - old_price) / old_price * 100.0)
    }

    /// Get the highest high over the last `n` candles.
    fn recent_high(&self, n: usize) -> Option<f64> {
        if self.points.is_empty() {
            return None;
        }
        let start = self.points.len().saturating_sub(n);
        self.points[start..]
            .iter()
            .map(|p| p.high)
            .fold(None, |acc: Option<f64>, h| {
                Some(acc.map_or(h, |a: f64| a.max(h)))
            })
    }

    /// Get the lowest low over the last `n` candles.
    fn recent_low(&self, n: usize) -> Option<f64> {
        if self.points.is_empty() {
            return None;
        }
        let start = self.points.len().saturating_sub(n);
        self.points[start..]
            .iter()
            .map(|p| p.low)
            .fold(None, |acc: Option<f64>, l| {
                Some(acc.map_or(l, |a: f64| a.min(l)))
            })
    }

    /// Get the last price point.
    fn last(&self) -> Option<&PricePoint> {
        self.points.last()
    }

    fn clear(&mut self) {
        self.points.clear();
    }
}

