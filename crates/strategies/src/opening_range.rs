    // ── Re-entry control ────────────────────────────────────────────────

    #[test]
    fn test_no_reentry_by_default() {
        let mut strategy = OrbStrategy::new(OrbConfig {
            opening_range_candles: 2,
            min_confidence: 0.45,
            min_volume_multiplier: 0.5,
            min_body_ratio: 0.3,
            allow_reentry: false,
            ..Default::default()
        });

        warm_up_atr(&mut strategy, 100.0, 20);
        strategy.start_session();

        strategy.update_ohlcv(100.0, 105.0, 95.0, 102.0, 1000.0);
        strategy.update_ohlcv(102.0, 104.0, 96.0, 103.0, 1000.0);

        // First breakout fires
        let r1 = strategy.update_ohlcv(104.0, 112.0, 103.0, 110.0, 1500.0);
        assert_eq!(r1.signal, OrbSignal::BuyBreakout);

        // Exit the position manually
        strategy.exit_position();

        // Second breakout attempt — should be blocked
        let r2 = strategy.update_ohlcv(109.0, 115.0, 108.0, 114.0, 2000.0);
        assert_eq!(r2.signal, OrbSignal::Hold);
    }

    #[test]
    fn test_reentry_when_allowed() {
        let mut strategy = OrbStrategy::new(OrbConfig {
            opening_range_candles: 2,
            min_confidence: 0.45,
            min_volume_multiplier: 0.5,
            min_body_ratio: 0.3,
            allow_reentry: true,
            ..Default::default()
        });

        warm_up_atr(&mut strategy, 100.0, 20);
        strategy.start_session();

        strategy.update_ohlcv(100.0, 105.0, 95.0, 102.0, 1000.0);
        strategy.update_ohlcv(102.0, 104.0, 96.0, 103.0, 1000.0);

        // First breakout
        let r1 = strategy.update_ohlcv(104.0, 112.0, 103.0, 110.0, 1500.0);
        assert_eq!(r1.signal, OrbSignal::BuyBreakout);

        // Exit the position manually
        strategy.exit_position();

        // Second breakout — should be allowed
        let r2 = strategy.update_ohlcv(109.0, 115.0, 108.0, 114.0, 2000.0);
        // May or may not fire depending on in_position logic, but won't be
        // blocked by the breakout_fired guard
        assert!(
            r2.signal == OrbSignal::BuyBreakout || r2.signal == OrbSignal::Hold,
            "Re-entry should not be blocked, got {:?}",
            r2.signal
        );
    }

