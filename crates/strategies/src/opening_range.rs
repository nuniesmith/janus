    // ── Stress test ─────────────────────────────────────────────────────

    #[test]
    fn test_many_candles_no_panic() {
        let mut strategy = OrbStrategy::new(OrbConfig::crypto_aggressive());

        warm_up_atr(&mut strategy, 100.0, 20);

        let mut sessions = 0;
        let mut breakouts = 0;

        for session in 0..5 {
            strategy.start_session();
            sessions += 1;

            for i in 0..50 {
                let base = 100.0 + (session as f64) * 10.0;
                let noise = (i as f64 * 0.2).sin() * 8.0;
                let close = base + noise;
                let result = strategy.update_ohlcv(
                    close - 1.0,
                    close + 12.0,
                    close - 12.0,
                    close,
                    1000.0 + (i as f64 * 50.0),
                );
                if result.signal == OrbSignal::BuyBreakout
                    || result.signal == OrbSignal::SellBreakout
                {
                    breakouts += 1;
                }
            }
        }

        println!(
            "Stress test: {} sessions, {} breakouts",
            sessions, breakouts
        );
        assert!(strategy.candle_count() > 200);
    }

    // ── End-to-end test ─────────────────────────────────────────────────

    #[test]
    fn test_end_to_end_session() {
        let mut strategy = OrbStrategy::new(OrbConfig {
            opening_range_candles: 3,
            min_confidence: 0.45,
            min_volume_multiplier: 0.5,
            min_body_ratio: 0.3,
            ..Default::default()
        });

        // Warm up ATR
        for i in 0..20 {
            let p = 100.0 + (i as f64 * 0.5).sin() * 3.0;
            strategy.update_ohlcv(p, p + 2.0, p - 2.0, p, 1000.0);
        }

        // Start session
        strategy.start_session();

        // Form opening range: 3 candles, range [95, 105]
        let r = strategy.update_ohlcv(100.0, 103.0, 97.0, 101.0, 1000.0);
        assert_eq!(r.signal, OrbSignal::Forming);

        let r = strategy.update_ohlcv(101.0, 105.0, 96.0, 103.0, 1000.0);
        assert_eq!(r.signal, OrbSignal::Forming);

        let r = strategy.update_ohlcv(103.0, 104.0, 95.0, 100.0, 1000.0);
        assert_eq!(r.signal, OrbSignal::Forming);
        assert!(strategy.is_range_complete());
        assert_eq!(strategy.range_high(), Some(105.0));
        assert_eq!(strategy.range_low(), Some(95.0));

        // Candle inside range → Hold
        let r = strategy.update_ohlcv(100.0, 104.0, 96.0, 102.0, 1000.0);
        assert_eq!(r.signal, OrbSignal::Hold);

        // Bullish breakout candle
        let r = strategy.update_ohlcv(104.0, 112.0, 103.0, 110.0, 2000.0);
        assert_eq!(r.signal, OrbSignal::BuyBreakout);
        assert!(r.stop_loss.is_some());
        assert!(r.take_profit.is_some());
        assert!(r.confidence >= 0.45);

        // Stop should be below OR high
        assert!(r.stop_loss.unwrap() < 105.0);
        // TP should be above entry
        assert!(r.take_profit.unwrap() > 110.0);
    }
