/// Configuration for the Opening Range Breakout strategy.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct OrbConfig {
    /// Number of candles that define the opening range (default: 6).
    /// E.g. on a 5-minute chart, 6 candles = 30-minute opening range.
    pub opening_range_candles: usize,

    /// ATR period for stop-loss / volatility measurement (default: 14).
    pub atr_period: usize,

    /// ATR multiplier for stop offset inside the opening range boundary
    /// (default: 0.5). Stop = OR boundary − (ATR × this value).
    pub atr_stop_offset: f64,

    /// Take-profit multiplier applied to the opening range size
    /// (default: 1.5). TP = breakout price + range_size × this value.
    pub range_tp_multiplier: f64,

    /// Rolling volume lookback period for spike detection (default: 20).
    pub volume_period: usize,

    /// Minimum volume multiplier (vs rolling average) for a valid breakout
    /// (default: 1.2). Higher values require stronger volume confirmation.
    pub min_volume_multiplier: f64,

    /// Minimum confidence score to emit a signal (default: 0.55).
    pub min_confidence: f64,

    /// Minimum candle body ratio (body / range) for a "strong" breakout
    /// candle (default: 0.5).
    pub min_body_ratio: f64,

    /// Whether to allow re-entry after the first breakout in the same
    /// session (default: false). When false, only the first breakout
    /// per session is traded.
    pub allow_reentry: bool,
}

impl Default for OrbConfig {
    fn default() -> Self {
        Self {
            opening_range_candles: 6,
            atr_period: 14,
            atr_stop_offset: 0.5,
            range_tp_multiplier: 1.5,
            volume_period: 20,
            min_volume_multiplier: 1.2,
            min_confidence: 0.55,
            min_body_ratio: 0.5,
            allow_reentry: false,
        }
    }
}

impl OrbConfig {
    /// Aggressive config for crypto — shorter opening range, lower thresholds.
    pub fn crypto_aggressive() -> Self {
        Self {
            opening_range_candles: 4,
            atr_stop_offset: 0.3,
            range_tp_multiplier: 2.0,
            min_volume_multiplier: 1.0,
            min_confidence: 0.50,
            min_body_ratio: 0.45,
            allow_reentry: true,
            ..Default::default()
        }
    }

    /// Conservative config — longer opening range, stricter thresholds.
    pub fn conservative() -> Self {
        Self {
            opening_range_candles: 10,
            atr_stop_offset: 0.7,
            range_tp_multiplier: 1.2,
            min_volume_multiplier: 1.5,
            min_confidence: 0.65,
            min_body_ratio: 0.55,
            allow_reentry: false,
            ..Default::default()
        }
    }
}

