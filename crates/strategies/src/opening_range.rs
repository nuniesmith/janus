    // Helper: warm up ATR with some candles before starting a session
    fn warm_up_atr(strategy: &mut OrbStrategy, base_price: f64, count: usize) {
        for i in 0..count {
            let noise = (i as f64 * 0.3).sin() * 5.0;
            let close = base_price + noise;
            // Feed directly to ATR + volume (not via update_ohlcv, to avoid
            // session logic). Instead we use update_ohlcv with no session.
            strategy.update_ohlcv(
                close - 1.0,
                close + 10.0,
                close - 10.0,
                close,
                1000.0 + i as f64 * 10.0,
            );
        }
    }

