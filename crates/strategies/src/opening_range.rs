/// Opening Range Breakout Strategy
///
/// Tracks the opening range for a trading session and generates breakout
/// signals when price escapes the range with confirmation. Single-asset —
/// instantiate one per symbol.
#[derive(Debug)]
pub struct OrbStrategy {
    config: OrbConfig,

    // Components
    range: OpeningRangeTracker,
    atr: ATR,
    volume_analyzer: VolumeAnalyzer,

    // Position tracking
    in_position: bool,
    entry_price: Option<f64>,
    entry_side: Option<OrbSignal>,
    stop_loss: Option<f64>,
    take_profit: Option<f64>,

    // Session tracking
    breakout_fired: bool,

    // Last computed values
    last_atr: Option<f64>,
    last_signal: OrbSignal,
    candle_count: usize,
}

impl OrbStrategy {
    /// Create a new Opening Range Breakout strategy with the given config.
    pub fn new(config: OrbConfig) -> Self {
        let atr = ATR::new(config.atr_period);
        let volume_analyzer = VolumeAnalyzer::new(config.volume_period);
        let range = OpeningRangeTracker::new(config.opening_range_candles);

        Self {
            range,
            atr,
            volume_analyzer,
            config,
            in_position: false,
            entry_price: None,
            entry_side: None,
            stop_loss: None,
            take_profit: None,
            breakout_fired: false,
            last_atr: None,
            last_signal: OrbSignal::Hold,
            candle_count: 0,
        }
    }

    /// Create with default configuration.
    pub fn with_defaults() -> Self {
        Self::new(OrbConfig::default())
    }

    /// Start a new trading session. Must be called before the strategy
    /// can generate breakout signals.
    ///
    /// For crypto (24/7 markets), call this at your chosen session boundary
    /// (e.g. 00:00 UTC, London open at 08:00 UTC, or NY open at 13:00 UTC).
    pub fn start_session(&mut self) {
        self.range.start_session();
        self.breakout_fired = false;
        self.exit_position();
    }

    /// Update the strategy with a `janus-models` Candle.
    pub fn update_candle(&mut self, candle: &janus_models::Candle) -> OrbSignal {
        self.update_ohlcv(
            candle.open,
            candle.high,
            candle.low,
            candle.close,
            candle.volume,
        )
        .signal
    }

    /// Update the strategy with a `janus-models` Candle and return detailed result.
    pub fn update_candle_with_reason(&mut self, candle: &janus_models::Candle) -> OrbResult {
        self.update_ohlcv(
            candle.open,
            candle.high,
            candle.low,
            candle.close,
            candle.volume,
        )
    }

    /// Update with OHLCV values and return detailed result.
    pub fn update_ohlcv(
        &mut self,
        open: f64,
        high: f64,
        low: f64,
        close: f64,
        volume: f64,
    ) -> OrbResult {
        self.candle_count += 1;

        // Update ATR (always, even before session starts)
        self.atr.update(high, low, close);
        self.last_atr = self.atr.value();

        // Update volume analyzer
        let vol_mult = self.volume_analyzer.update(volume);

        // No session active
        if !self.range.session_active {
            return self.make_result(
                OrbSignal::Hold,
                vol_mult,
                0.0,
                "No session active — call start_session()".to_string(),
            );
        }

        // Still forming the opening range
        if !self.range.is_complete {
            self.range.update(high, low);

            if self.range.is_complete {
                return self.make_result(
                    OrbSignal::Forming,
                    vol_mult,
                    0.0,
                    format!(
                        "Opening range complete: high={:.2}, low={:.2}, size={:.2}",
                        self.range.high,
                        self.range.low,
                        self.range.range_size()
                    ),
                );
            }

            return self.make_result(
                OrbSignal::Forming,
                vol_mult,
                0.0,
                format!(
                    "Forming opening range: {}/{} candles, high={:.2}, low={:.2}",
                    self.range.candles_seen,
                    self.range.range_candles,
                    if self.range.high > f64::NEG_INFINITY {
                        self.range.high
                    } else {
                        0.0
                    },
                    if self.range.low < f64::INFINITY {
                        self.range.low
                    } else {
                        0.0
                    },
                ),
            );
        }

        let or_high = self.range.high;
        let or_low = self.range.low;
        let or_range = self.range.range_size();

        if or_range <= 0.0 {
            return self.make_result(
                OrbSignal::Hold,
                vol_mult,
                0.0,
                "Opening range has zero size".to_string(),
            );
        }

        // Check if breakout already fired (no re-entry unless configured)
        if self.breakout_fired && !self.config.allow_reentry {
            return self.make_result(
                OrbSignal::Hold,
                vol_mult,
                0.0,
                "Breakout already fired this session".to_string(),
            );
        }

        let atr_val = match self.atr.value() {
            Some(a) if a > 0.0 => a,
            _ => {
                return self.make_result(
                    OrbSignal::Hold,
                    vol_mult,
                    0.0,
                    "ATR not ready".to_string(),
                );
            }
        };

        // Candle properties
        let body = (close - open).abs();
        let range_candle = high - low;
        let is_bullish = close > open;
        let is_bearish = close < open;
        let body_ratio = if range_candle > 0.0 {
            body / range_candle
        } else {
            0.0
        };

        // Check for exit if in position
        if self.in_position
            && let Some(ref side) = self.entry_side
        {
            let exit = self.check_exit(close, side);
            if let Some(exit_signal) = exit {
                self.exit_position();
                return self.make_result(
                    exit_signal,
                    vol_mult,
                    0.8,
                    format!("ORB exit: price {:.2} reached TP/SL", close),
                );
            }
        }

        // Already in position — don't open another
        if self.in_position {
            return self.make_result(OrbSignal::Hold, vol_mult, 0.0, "In position".to_string());
        }

        // === BULLISH BREAKOUT ===
        if close > or_high && is_bullish {
            let mut confidence: f64 = 0.50;

            // Volume confirmation
            if vol_mult >= self.config.min_volume_multiplier {
                confidence += 0.15;
            }

            // Strong candle body
            if body_ratio >= self.config.min_body_ratio {
                confidence += 0.10;
            }

            // How far above the range — modest extension is ideal
            let extension_pct = (close - or_high) / or_range;
            if extension_pct > 0.0 && extension_pct < 0.5 {
                // Clean breakout, not too extended
                confidence += 0.10;
            }

            confidence = confidence.min(1.0);

            if confidence >= self.config.min_confidence {
                let stop = or_high - atr_val * self.config.atr_stop_offset;
                let tp = close + or_range * self.config.range_tp_multiplier;

                self.enter_position(OrbSignal::BuyBreakout, close, stop, tp);
                self.breakout_fired = true;
                self.last_signal = OrbSignal::BuyBreakout;

                return self.make_result(
                    OrbSignal::BuyBreakout,
                    vol_mult,
                    confidence,
                    format!(
                        "ORB bullish breakout: price {:.2} > OR high {:.2}, range {:.2}, vol {:.1}x",
                        close, or_high, or_range, vol_mult
                    ),
                );
            }
        }

        // === BEARISH BREAKOUT ===
        if close < or_low && is_bearish {
            let mut confidence: f64 = 0.50;

            // Volume confirmation
            if vol_mult >= self.config.min_volume_multiplier {
                confidence += 0.15;
            }

            // Strong candle body
            if body_ratio >= self.config.min_body_ratio {
                confidence += 0.10;
            }

            // How far below the range
            let extension_pct = (or_low - close) / or_range;
            if extension_pct > 0.0 && extension_pct < 0.5 {
                confidence += 0.10;
            }

            confidence = confidence.min(1.0);

            if confidence >= self.config.min_confidence {
                let stop = or_low + atr_val * self.config.atr_stop_offset;
                let tp = close - or_range * self.config.range_tp_multiplier;

                self.enter_position(OrbSignal::SellBreakout, close, stop, tp);
                self.breakout_fired = true;
                self.last_signal = OrbSignal::SellBreakout;

                return self.make_result(
                    OrbSignal::SellBreakout,
                    vol_mult,
                    confidence,
                    format!(
                        "ORB bearish breakout: price {:.2} < OR low {:.2}, range {:.2}, vol {:.1}x",
                        close, or_low, or_range, vol_mult
                    ),
                );
            }
        }

        self.last_signal = OrbSignal::Hold;
        self.make_result(
            OrbSignal::Hold,
            vol_mult,
            0.0,
            format!(
                "No breakout: price {:.2}, OR [{:.2}, {:.2}]",
                close, or_low, or_high
            ),
        )
    }

    /// Update with HLC values (no volume — uses volume=0.0 / fallback).
    pub fn update_hlc(&mut self, high: f64, low: f64, close: f64) -> OrbResult {
        self.update_ohlcv(close, high, low, close, 0.0)
    }

    // ========================================================================
    // Regime gating
    // ========================================================================

    /// Check whether this strategy should trade in the given market regime.
    ///
    /// ORB is a breakout/momentum strategy. It fires when price escapes a
    /// consolidation range, so it works best at regime transitions.
    pub fn should_trade_in_regime(regime: &MarketRegime) -> bool {
        matches!(
            regime,
            MarketRegime::MeanReverting | MarketRegime::Uncertain
        )
    }

    /// Compute a confidence-weighted position size factor for the given regime.
    ///
    /// Returns `Some(factor)` if the regime is suitable, `None` otherwise.
    pub fn regime_size_factor(regime: &MarketRegime, confidence: f64) -> Option<f64> {
        match regime {
            MarketRegime::MeanReverting => {
                // ORB from a ranging market — breakout likely to run
                Some(confidence.clamp(0.4, 1.0))
            }
            MarketRegime::Uncertain => {
                // May be transitioning — allow with reduced size
                Some(if confidence >= 0.5 { 0.35 } else { 0.20 })
            }
            _ => None,
        }
    }

    // ========================================================================
    // Position management (internal)
    // ========================================================================

    fn enter_position(&mut self, side: OrbSignal, price: f64, stop: f64, tp: f64) {
        self.in_position = true;
        self.entry_price = Some(price);
        self.entry_side = Some(side);
        self.stop_loss = Some(stop);
        self.take_profit = Some(tp);
    }

    fn check_exit(&self, price: f64, side: &OrbSignal) -> Option<OrbSignal> {
        let stop = self.stop_loss.unwrap_or(0.0);
        let tp = self.take_profit.unwrap_or(price);

        match side {
            OrbSignal::BuyBreakout => {
                if price >= tp || price <= stop {
                    Some(OrbSignal::SellBreakout)
                } else {
                    None
                }
            }
            OrbSignal::SellBreakout => {
                if price <= tp || price >= stop {
                    Some(OrbSignal::BuyBreakout)
                } else {
                    None
                }
            }
            _ => None,
        }
    }

    fn exit_position(&mut self) {
        self.in_position = false;
        self.entry_price = None;
        self.entry_side = None;
        self.stop_loss = None;
        self.take_profit = None;
    }

    fn make_result(
        &self,
        signal: OrbSignal,
        volume_multiplier: f64,
        confidence: f64,
        reason: String,
    ) -> OrbResult {
        let (rh, rl, rs) = if self.range.is_complete
            && self.range.high > f64::NEG_INFINITY
            && self.range.low < f64::INFINITY
        {
            (
                Some(self.range.high),
                Some(self.range.low),
                Some(self.range.range_size()),
            )
        } else {
            (None, None, None)
        };

        OrbResult {
            signal,
            range_high: rh,
            range_low: rl,
            range_size: rs,
            atr: self.last_atr,
            volume_multiplier,
            confidence,
            stop_loss: if signal == OrbSignal::BuyBreakout || signal == OrbSignal::SellBreakout {
                self.stop_loss
            } else {
                None
            },
            take_profit: if signal == OrbSignal::BuyBreakout || signal == OrbSignal::SellBreakout {
                self.take_profit
            } else {
                None
            },
            reason,
        }
    }

    // ========================================================================
    // Full reset
    // ========================================================================

    /// Full reset — clears all state including positions, opening range,
    /// and indicators.
    pub fn reset(&mut self) {
        self.range = OpeningRangeTracker::new(self.config.opening_range_candles);
        self.atr.reset();
        self.volume_analyzer = VolumeAnalyzer::new(self.config.volume_period);
        self.in_position = false;
        self.entry_price = None;
        self.entry_side = None;
        self.stop_loss = None;
        self.take_profit = None;
        self.breakout_fired = false;
        self.last_atr = None;
        self.last_signal = OrbSignal::Hold;
        self.candle_count = 0;
    }

    // ========================================================================
    // Accessors
    // ========================================================================

    /// Whether the opening range is fully formed.
    pub fn is_range_complete(&self) -> bool {
        self.range.is_complete
    }

    /// Whether a session is currently active.
    pub fn is_session_active(&self) -> bool {
        self.range.session_active
    }

    /// Whether the strategy currently tracks an open position.
    pub fn is_in_position(&self) -> bool {
        self.in_position
    }

    /// Whether a breakout has already been fired in this session.
    pub fn breakout_fired(&self) -> bool {
        self.breakout_fired
    }

    /// Opening range high (if complete).
    pub fn range_high(&self) -> Option<f64> {
        if self.range.is_complete {
            Some(self.range.high)
        } else {
            None
        }
    }

    /// Opening range low (if complete).
    pub fn range_low(&self) -> Option<f64> {
        if self.range.is_complete {
            Some(self.range.low)
        } else {
            None
        }
    }

    /// Opening range size (if complete).
    pub fn range_size(&self) -> Option<f64> {
        if self.range.is_complete {
            Some(self.range.range_size())
        } else {
            None
        }
    }

    /// Current ATR value (if available).
    pub fn last_atr(&self) -> Option<f64> {
        self.last_atr
    }

    /// Last signal produced.
    pub fn last_signal(&self) -> OrbSignal {
        self.last_signal
    }

    /// Number of candles processed since creation / last reset.
    pub fn candle_count(&self) -> usize {
        self.candle_count
    }

    /// Entry price of the current position (if any).
    pub fn entry_price(&self) -> Option<f64> {
        self.entry_price
    }

    /// Stop loss of the current position (if any).
    pub fn stop_loss(&self) -> Option<f64> {
        self.stop_loss
    }

    /// Take profit of the current position (if any).
    pub fn take_profit(&self) -> Option<f64> {
        self.take_profit
    }

    /// Reference to the strategy configuration.
    pub fn config(&self) -> &OrbConfig {
        &self.config
    }
}

