//! # Opening Range Breakout (ORB) Strategy
//!
//! Defines the first N candles as the "opening range" and trades breakouts
//! above or below that range with volume confirmation. Targets 1–2× range
//! extension.
//!
//! ## How It Works
//!
//! 1. **Opening Range Formation**: The first `opening_range_candles` candles
//!    after a session start (or after calling `start_session()`) define the
//!    opening range — the highest high and lowest low during that period.
//!
//! 2. **Breakout Detection**: Once the opening range is complete, a close
//!    above the range high (bullish) or below the range low (bearish)
//!    triggers a breakout signal.
//!
//! 3. **Volume Confirmation**: Breakout candles with above-average volume
//!    receive a confidence boost. Strong candle bodies (large body relative
//!    to range) also add confidence.
//!
//! 4. **ATR-Based Stops**: Stop loss is placed at 0.5× ATR inside the
//!    opening range boundary. Take profit targets 1.5× the opening range
//!    size as a range extension.
//!
//! 5. **Session Reset**: Call `start_session()` at the beginning of each
//!    trading session to reset the opening range. For 24/7 crypto markets,
//!    use London or NY overlap as the session start.
//!
//! ## Regime Integration
//!
//! ORB works best when the market is transitioning from consolidation to
//! trend — it's a momentum/breakout strategy. Use `should_trade_in_regime()`
//! to gate activation:
//!
//! - **MeanReverting** → Yes (breakout from the range)
//! - **Uncertain** → Yes with reduced size (range may be forming)
//! - **Trending** → Conditional — only early in a trend
//! - **Volatile** → No (false breakouts are common)
//!
//! ## Origin
//!
//! Ported from Kraken's `strategy/intraday.rs::OpeningRangeBreakout`, adapted
//! for the JANUS single-asset design, `janus-regime` indicator types, and
//! regime-aware position sizing.

use janus_regime::MarketRegime;
use janus_regime::indicators::ATR;
use serde::{Deserialize, Serialize};

// ============================================================================
// Signal
// ============================================================================

/// Signal generated by the Opening Range Breakout strategy.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum OrbSignal {
    /// Bullish breakout — price closed above the opening range high.
    BuyBreakout,
    /// Bearish breakout — price closed below the opening range low.
    SellBreakout,
    /// Opening range is still forming — no action yet.
    Forming,
    /// No signal — range is complete but no breakout detected.
    Hold,
}

impl std::fmt::Display for OrbSignal {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            OrbSignal::BuyBreakout => write!(f, "BuyBreakout"),
            OrbSignal::SellBreakout => write!(f, "SellBreakout"),
            OrbSignal::Forming => write!(f, "Forming"),
            OrbSignal::Hold => write!(f, "Hold"),
        }
    }
}

