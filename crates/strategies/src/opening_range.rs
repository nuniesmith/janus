/// Detailed result from an ORB strategy update.
#[derive(Debug, Clone)]
pub struct OrbResult {
    /// The signal produced.
    pub signal: OrbSignal,
    /// Opening range high (if complete).
    pub range_high: Option<f64>,
    /// Opening range low (if complete).
    pub range_low: Option<f64>,
    /// Opening range size (high - low).
    pub range_size: Option<f64>,
    /// Current ATR value (if available).
    pub atr: Option<f64>,
    /// Volume multiplier relative to rolling average.
    pub volume_multiplier: f64,
    /// Confidence score (0.0–1.0).
    pub confidence: f64,
    /// Computed stop-loss price (only set on breakout signals).
    pub stop_loss: Option<f64>,
    /// Computed take-profit price (only set on breakout signals).
    pub take_profit: Option<f64>,
    /// Human-readable reason string.
    pub reason: String,
}

// ============================================================================
// Opening Range Tracker (internal)
// ============================================================================

/// Tracks the opening range for the current session.
#[derive(Debug)]
struct OpeningRangeTracker {
    /// Number of candles that define the range.
    range_candles: usize,
    /// High of the opening range.
    high: f64,
    /// Low of the opening range.
    low: f64,
    /// Number of candles seen so far in this session.
    candles_seen: usize,
    /// Whether the opening range is fully formed.
    is_complete: bool,
    /// Whether a session has been started.
    session_active: bool,
}

impl OpeningRangeTracker {
    fn new(range_candles: usize) -> Self {
        Self {
            range_candles: range_candles.max(1),
            high: f64::NEG_INFINITY,
            low: f64::INFINITY,
            candles_seen: 0,
            is_complete: false,
            session_active: false,
        }
    }

    /// Start a new session — resets the opening range.
    fn start_session(&mut self) {
        self.high = f64::NEG_INFINITY;
        self.low = f64::INFINITY;
        self.candles_seen = 0;
        self.is_complete = false;
        self.session_active = true;
    }

    /// Feed a candle to the opening range tracker.
    fn update(&mut self, high: f64, low: f64) {
        if !self.session_active || self.is_complete {
            return;
        }

        self.high = self.high.max(high);
        self.low = self.low.min(low);
        self.candles_seen += 1;

        if self.candles_seen >= self.range_candles {
            self.is_complete = true;
        }
    }

    /// The size of the opening range (high - low).
    fn range_size(&self) -> f64 {
        if self.high > f64::NEG_INFINITY && self.low < f64::INFINITY {
            self.high - self.low
        } else {
            0.0
        }
    }
}

// ============================================================================
// Volume Analyzer (internal)
// ============================================================================

/// Rolling volume analyzer — detects volume spikes relative to the moving
/// average of recent candle volumes.
#[derive(Debug)]
struct VolumeAnalyzer {
    period: usize,
    volumes: Vec<f64>,
}

impl VolumeAnalyzer {
    fn new(period: usize) -> Self {
        Self {
            period: period.max(1),
            volumes: Vec::with_capacity(period),
        }
    }

    /// Push a new volume value. Returns the multiplier (volume / rolling avg).
    /// Returns 1.0 if there's no history yet.
    fn update(&mut self, volume: f64) -> f64 {
        let avg = self.average();

        self.volumes.push(volume);
        if self.volumes.len() > self.period {
            self.volumes.remove(0);
        }

        if avg > 0.0 { volume / avg } else { 1.0 }
    }

    /// Rolling average volume over the configured period.
    fn average(&self) -> f64 {
        if self.volumes.is_empty() {
            0.0
        } else {
            self.volumes.iter().sum::<f64>() / self.volumes.len() as f64
        }
    }
}

