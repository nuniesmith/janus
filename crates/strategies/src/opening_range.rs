    // ── Opening range formation ─────────────────────────────────────────

    #[test]
    fn test_opening_range_formation() {
        let mut strategy = OrbStrategy::new(OrbConfig {
            opening_range_candles: 3,
            ..Default::default()
        });

        // Warm up ATR
        warm_up_atr(&mut strategy, 100.0, 20);

        strategy.start_session();

        // First candle
        let r1 = strategy.update_ohlcv(100.0, 105.0, 95.0, 102.0, 1000.0);
        assert_eq!(r1.signal, OrbSignal::Forming);
        assert!(!strategy.is_range_complete());

        // Second candle
        let r2 = strategy.update_ohlcv(102.0, 108.0, 97.0, 104.0, 1000.0);
        assert_eq!(r2.signal, OrbSignal::Forming);
        assert!(!strategy.is_range_complete());

        // Third candle — completes the range
        let r3 = strategy.update_ohlcv(104.0, 106.0, 99.0, 103.0, 1000.0);
        assert_eq!(r3.signal, OrbSignal::Forming);
        assert!(strategy.is_range_complete());

        // Verify range boundaries
        assert_eq!(strategy.range_high(), Some(108.0)); // Max high
        assert_eq!(strategy.range_low(), Some(95.0)); // Min low
        assert!((strategy.range_size().unwrap() - 13.0).abs() < f64::EPSILON);
    }

    // ── Breakout detection ──────────────────────────────────────────────

    #[test]
    fn test_bullish_breakout() {
        let mut strategy = OrbStrategy::new(OrbConfig {
            opening_range_candles: 2,
            min_confidence: 0.45,
            min_volume_multiplier: 0.5,
            min_body_ratio: 0.3,
            ..Default::default()
        });

        warm_up_atr(&mut strategy, 100.0, 20);
        strategy.start_session();

        // Form opening range: high=105, low=95
        strategy.update_ohlcv(100.0, 105.0, 95.0, 102.0, 1000.0);
        strategy.update_ohlcv(102.0, 104.0, 96.0, 103.0, 1000.0);

        assert!(strategy.is_range_complete());

        // Breakout above: bullish candle closing above 105
        let result = strategy.update_ohlcv(104.0, 112.0, 103.0, 110.0, 1500.0);

        assert_eq!(result.signal, OrbSignal::BuyBreakout);
        assert!(result.confidence >= 0.45);
        assert!(result.stop_loss.is_some());
        assert!(result.take_profit.is_some());
        assert!(strategy.breakout_fired());
    }

