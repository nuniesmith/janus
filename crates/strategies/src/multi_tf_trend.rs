//! # Multi-Timeframe Trend Strategy
//!
//! Swing-timeframe trend strategy using EMA 50/200 crossover with ADX trend
//! strength confirmation and higher-timeframe (HTF) trend alignment.
//!
//! ## How It Works
//!
//! 1. **Trend Direction**: EMA 50 vs EMA 200 determines the primary trend.
//!    Price must be on the correct side of the 200 EMA.
//!
//! 2. **Trend Strength**: ADX must be above a configurable threshold (default:
//!    25.0) to confirm that the market is actually trending, not just drifting.
//!
//! 3. **Pullback Entry**: Rather than entering on the initial crossover, the
//!    strategy waits for price to pull back to the 50 EMA within an
//!    established trend. This gives better risk/reward.
//!
//! 4. **HTF Alignment**: An externally-set higher-timeframe trend bias must
//!    agree with the setup direction. This prevents counter-trend entries on
//!    the lower timeframe.
//!
//! 5. **Candle Confirmation**: The entry candle must be directionally aligned
//!    with a strong body (body > 60% of range).
//!
//! 6. **ATR-Based Stops**: Stop loss at the 200 EMA ± ATR multiple, take
//!    profit at a larger ATR multiple from entry.
//!
//! ## Regime Integration
//!
//! This is a pure trend-following strategy:
//!
//! - **Trending(Bullish/Bearish)** → Yes (primary use case)
//! - **Volatile** → No (ADX can be misleading in volatile chop)
//! - **MeanReverting** → No (no trend to follow)
//! - **Uncertain** → No (wait for clarity)
//!
//! ## Differences from Kraken Version
//!
//! The Kraken `MultiTimeframeTrend` used a multi-symbol HashMap and its own
//! local indicator types. This JANUS version uses `janus-regime` indicators
//! (EMA, ATR, ADX) and follows the single-asset pattern established by
//! other JANUS strategies (VwapScalper, OrbStrategy, etc.).
//!
//! The strategy requires more candles to warm up (200+ for the long EMA) and
//! is intended for higher-timeframe candles. In the JANUS pipeline, this can
//! be fed from a separate candle aggregator with a larger tick-per-candle
//! setting, or from pre-aggregated candle data.
//!
//! ## Origin
//!
//! Ported from Kraken's `strategy/swing.rs::MultiTimeframeTrend`, adapted for
//! the JANUS single-asset design, `janus-regime` indicator types, and
//! regime-aware position sizing.

use janus_regime::MarketRegime;
use janus_regime::indicators::{ADX, ATR, EMA};
use janus_regime::types::TrendDirection;
use serde::{Deserialize, Serialize};

// ============================================================================
// Signal Types
// ============================================================================

/// Signal generated by the Multi-Timeframe Trend strategy.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum MultiTfSignal {
    /// Enter long — bullish trend pullback to EMA 50 with ADX strength.
    Buy,
    /// Enter short / exit long — bearish trend pullback with ADX strength.
    Sell,
    /// No action.
    Hold,
}

impl std::fmt::Display for MultiTfSignal {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            MultiTfSignal::Buy => write!(f, "BUY"),
            MultiTfSignal::Sell => write!(f, "SELL"),
            MultiTfSignal::Hold => write!(f, "HOLD"),
        }
    }
}

