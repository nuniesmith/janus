/// Configuration for the Multi-Timeframe Trend strategy.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct MultiTfConfig {
    /// Short EMA period (default: 50).
    pub ema_short_period: usize,

    /// Long EMA period (default: 200).
    pub ema_long_period: usize,

    /// ADX period for trend strength measurement (default: 14).
    pub adx_period: usize,

    /// ADX threshold — values above this confirm a trending market (default: 25.0).
    pub adx_threshold: f64,

    /// ATR period for volatility measurement (default: 14).
    pub atr_period: usize,

    /// ATR multiplier for stop-loss distance (default: 2.0).
    pub atr_stop_multiplier: f64,

    /// ATR multiplier for take-profit distance (default: 4.0).
    pub atr_tp_multiplier: f64,

    /// Pullback tolerance — how far above/below the short EMA the price can
    /// be and still count as a "pullback". Expressed as a fraction of price
    /// (default: 0.01 = 1%).
    pub pullback_tolerance: f64,

    /// Minimum candle body ratio (body / range) for entry confirmation
    /// (default: 0.60).
    pub min_body_ratio: f64,

    /// Minimum confidence score to emit a signal (default: 0.55).
    pub min_confidence: f64,

    /// Minimum number of candles before the strategy is ready (default: 210).
    /// Must be at least `ema_long_period + 10` to ensure the 200 EMA has
    /// warmed up.
    pub warmup_candles: usize,
}

impl Default for MultiTfConfig {
    fn default() -> Self {
        Self {
            ema_short_period: 50,
            ema_long_period: 200,
            adx_period: 14,
            adx_threshold: 25.0,
            atr_period: 14,
            atr_stop_multiplier: 2.0,
            atr_tp_multiplier: 4.0,
            pullback_tolerance: 0.01,
            min_body_ratio: 0.60,
            min_confidence: 0.55,
            warmup_candles: 210,
        }
    }
}

impl MultiTfConfig {
    /// Aggressive config for crypto — lower ADX threshold, tighter stops.
    pub fn crypto_aggressive() -> Self {
        Self {
            adx_threshold: 20.0,
            atr_stop_multiplier: 1.5,
            atr_tp_multiplier: 3.5,
            pullback_tolerance: 0.015,
            min_body_ratio: 0.50,
            min_confidence: 0.50,
            warmup_candles: 205,
            ..Default::default()
        }
    }

    /// Conservative config — higher ADX threshold, wider stops.
    pub fn conservative() -> Self {
        Self {
            adx_threshold: 30.0,
            atr_stop_multiplier: 2.5,
            atr_tp_multiplier: 5.0,
            pullback_tolerance: 0.008,
            min_body_ratio: 0.65,
            min_confidence: 0.65,
            warmup_candles: 215,
            ..Default::default()
        }
    }
}

