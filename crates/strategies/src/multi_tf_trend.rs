/// Multi-Timeframe Trend Strategy
///
/// Swing-timeframe EMA 50/200 crossover with ADX trend strength and HTF
/// alignment. Single-asset — instantiate one per symbol.
#[derive(Debug)]
pub struct MultiTfTrendStrategy {
    config: MultiTfConfig,

    // Indicators
    ema_short: EMA,
    ema_long: EMA,
    adx: ADX,
    atr: ATR,

    // Position tracking
    in_position: bool,
    entry_price: Option<f64>,
    entry_side: Option<MultiTfSignal>,
    stop_loss: Option<f64>,
    take_profit: Option<f64>,

    // Last computed values (cached)
    last_ema_short: Option<f64>,
    last_ema_long: Option<f64>,
    last_adx: Option<f64>,
    last_atr: Option<f64>,
    last_signal: MultiTfSignal,
    candle_count: usize,

    // HTF trend bias (set externally from higher-timeframe analysis)
    htf_trend: Option<TrendDirection>,
}

impl MultiTfTrendStrategy {
    /// Create a new Multi-TF Trend strategy with the given config.
    pub fn new(config: MultiTfConfig) -> Self {
        let ema_short = EMA::new(config.ema_short_period);
        let ema_long = EMA::new(config.ema_long_period);
        let adx = ADX::new(config.adx_period);
        let atr = ATR::new(config.atr_period);

        Self {
            ema_short,
            ema_long,
            adx,
            atr,
            config,
            in_position: false,
            entry_price: None,
            entry_side: None,
            stop_loss: None,
            take_profit: None,
            last_ema_short: None,
            last_ema_long: None,
            last_adx: None,
            last_atr: None,
            last_signal: MultiTfSignal::Hold,
            candle_count: 0,
            htf_trend: None,
        }
    }

    /// Create with default configuration.
    pub fn with_defaults() -> Self {
        Self::new(MultiTfConfig::default())
    }

    /// Set higher-timeframe trend bias. When set, long setups require HTF
    /// bullish, short setups require HTF bearish. If not set (None or Flat
    /// equivalent), the strategy will not produce signals — HTF alignment
    /// is mandatory for this strategy.
    pub fn set_htf_trend(&mut self, trend: TrendDirection) {
        self.htf_trend = Some(trend);
    }

    /// Clear the HTF trend bias. Without an HTF trend, this strategy will
    /// not produce any signals.
    pub fn clear_htf_trend(&mut self) {
        self.htf_trend = None;
    }

    /// Update with a `janus-models` Candle.
    pub fn update_candle(&mut self, candle: &janus_models::Candle) -> MultiTfSignal {
        self.update_ohlcv(
            candle.open,
            candle.high,
            candle.low,
            candle.close,
            candle.volume,
        )
        .signal
    }

    /// Update with a `janus-models` Candle and return detailed result.
    pub fn update_candle_with_reason(&mut self, candle: &janus_models::Candle) -> MultiTfResult {
        self.update_ohlcv(
            candle.open,
            candle.high,
            candle.low,
            candle.close,
            candle.volume,
        )
    }

    /// Main update with OHLCV values — returns a detailed result.
    pub fn update_ohlcv(
        &mut self,
        open: f64,
        high: f64,
        low: f64,
        close: f64,
        _volume: f64,
    ) -> MultiTfResult {
        self.candle_count += 1;

        // Update indicators
        self.ema_short.update(close);
        self.ema_long.update(close);
        self.adx.update(high, low, close);
        self.atr.update(high, low, close);

        self.last_ema_short = self.ema_short.value();
        self.last_ema_long = self.ema_long.value();
        self.last_adx = self.adx.value();
        self.last_atr = self.atr.value();

        // Not ready yet?
        if self.candle_count < self.config.warmup_candles {
            return self.make_result(MultiTfSignal::Hold, 0.0, "Warming up".to_string());
        }

        let ema_s = match self.last_ema_short {
            Some(v) => v,
            None => {
                return self.make_result(
                    MultiTfSignal::Hold,
                    0.0,
                    "Short EMA not ready".to_string(),
                );
            }
        };
        let ema_l = match self.last_ema_long {
            Some(v) => v,
            None => {
                return self.make_result(
                    MultiTfSignal::Hold,
                    0.0,
                    "Long EMA not ready".to_string(),
                );
            }
        };
        let adx_val = self.last_adx.unwrap_or(0.0);
        let atr_val = match self.last_atr {
            Some(a) if a > 0.0 => a,
            _ => {
                return self.make_result(MultiTfSignal::Hold, 0.0, "ATR not ready".to_string());
            }
        };

        // Determine lower-timeframe trend from EMAs
        let ltf_trend = if ema_s > ema_l {
            TrendDirection::Bullish
        } else {
            TrendDirection::Bearish
        };

        // HTF trend is mandatory for this strategy
        let htf_trend = match self.htf_trend {
            Some(t) => t,
            None => {
                return self.make_result(
                    MultiTfSignal::Hold,
                    0.0,
                    "No HTF trend set — waiting for higher-timeframe signal".to_string(),
                );
            }
        };

        // Candle properties
        let body = (close - open).abs();
        let range = high - low;
        let is_bullish = close > open;
        let is_bearish = close < open;
        let body_ratio = if range > 0.0 { body / range } else { 0.0 };
        let price = close;

        // Check for exit if in position
        if self.in_position
            && let Some(ref side) = self.entry_side
        {
            let exit = self.check_exit(close, ema_l, atr_val, side);
            if let Some(exit_signal) = exit {
                self.exit_position();
                return self.make_result(
                    exit_signal,
                    0.8,
                    format!("MTF exit: price {:.2} (EMA200={:.2})", close, ema_l),
                );
            }
        }

        // Already in position — don't open another
        if self.in_position {
            return self.make_result(MultiTfSignal::Hold, 0.0, "In position".to_string());
        }

        // === BULLISH SETUP ===
        // HTF bullish + price > 200 EMA + ADX strong + pullback to 50 EMA + bullish candle
        if htf_trend == TrendDirection::Bullish
            && price > ema_l
            && adx_val >= self.config.adx_threshold
            && price <= ema_s * (1.0 + self.config.pullback_tolerance)
            && is_bullish
        {
            let stop = ema_l - atr_val * self.config.atr_stop_multiplier;
            let tp = price + atr_val * self.config.atr_tp_multiplier;

            let mut confidence: f64 = 0.40;

            // LTF trend agreement (EMA50 > EMA200)
            if ltf_trend == TrendDirection::Bullish {
                confidence += 0.15;
            }

            // Strong ADX
            if adx_val > 30.0 {
                confidence += 0.10;
            }

            // Strong candle body
            if body_ratio >= self.config.min_body_ratio {
                confidence += 0.10;
            }

            // Close above both EMAs
            if price > ema_s && price > ema_l {
                confidence += 0.05;
            }

            confidence = confidence.min(1.0);

            if confidence >= self.config.min_confidence {
                self.enter_position(MultiTfSignal::Buy, price, stop, tp);
                self.last_signal = MultiTfSignal::Buy;

                return self.make_result(
                    MultiTfSignal::Buy,
                    confidence,
                    format!(
                        "MTF bullish: pullback to EMA50 {:.2}, ADX {:.1}, price > EMA200 {:.2}, body {:.0}%",
                        ema_s, adx_val, ema_l, body_ratio * 100.0
                    ),
                );
            }
        }

        // === BEARISH SETUP ===
        // HTF bearish + price < 200 EMA + ADX strong + pullback to 50 EMA + bearish candle
        if htf_trend == TrendDirection::Bearish
            && price < ema_l
            && adx_val >= self.config.adx_threshold
            && price >= ema_s * (1.0 - self.config.pullback_tolerance)
            && is_bearish
        {
            let stop = ema_l + atr_val * self.config.atr_stop_multiplier;
            let tp = price - atr_val * self.config.atr_tp_multiplier;

            let mut confidence: f64 = 0.40;

            // LTF trend agreement
            if ltf_trend == TrendDirection::Bearish {
                confidence += 0.15;
            }

            // Strong ADX
            if adx_val > 30.0 {
                confidence += 0.10;
            }

            // Strong candle body
            if body_ratio >= self.config.min_body_ratio {
                confidence += 0.10;
            }

            // Close below both EMAs
            if price < ema_s && price < ema_l {
                confidence += 0.05;
            }

            confidence = confidence.min(1.0);

            if confidence >= self.config.min_confidence {
                self.enter_position(MultiTfSignal::Sell, price, stop, tp);
                self.last_signal = MultiTfSignal::Sell;

                return self.make_result(
                    MultiTfSignal::Sell,
                    confidence,
                    format!(
                        "MTF bearish: pullback to EMA50 {:.2}, ADX {:.1}, price < EMA200 {:.2}, body {:.0}%",
                        ema_s, adx_val, ema_l, body_ratio * 100.0
                    ),
                );
            }
        }

        self.last_signal = MultiTfSignal::Hold;
        self.make_result(
            MultiTfSignal::Hold,
            0.0,
            format!(
                "No signal: price {:.2}, EMA50={:.2}, EMA200={:.2}, ADX={:.1}, HTF={:?}, LTF={:?}",
                price, ema_s, ema_l, adx_val, htf_trend, ltf_trend
            ),
        )
    }

    /// Update with HLC values (no volume).
    pub fn update_hlc(&mut self, high: f64, low: f64, close: f64) -> MultiTfResult {
        self.update_ohlcv(close, high, low, close, 0.0)
    }

    // ========================================================================
    // Regime gating
    // ========================================================================

    /// Check whether this strategy should trade in the given market regime.
    ///
    /// Multi-TF Trend is purely trend-following — it only works in clearly
    /// trending markets. Unlike other trend strategies, it does NOT trade in
    /// volatile markets because ADX readings become unreliable during high
    /// volatility chop.
    pub fn should_trade_in_regime(regime: &MarketRegime) -> bool {
        matches!(regime, MarketRegime::Trending(_))
    }

    /// Compute a confidence-weighted position size factor for the given regime.
    ///
    /// Returns `Some(factor)` if the regime is suitable, `None` otherwise.
    pub fn regime_size_factor(regime: &MarketRegime, confidence: f64) -> Option<f64> {
        match regime {
            MarketRegime::Trending(_) => {
                // Primary regime — full confidence scaling
                Some(confidence.clamp(0.4, 1.0))
            }
            _ => None,
        }
    }

    // ========================================================================
    // Trend info accessor
    // ========================================================================

    /// Get the current trend info: (ltf_trend, adx_value).
    /// Returns None if indicators are not ready.
    pub fn get_trend_info(&self) -> Option<(TrendDirection, f64)> {
        let ema_s = self.last_ema_short?;
        let ema_l = self.last_ema_long?;
        let adx = self.last_adx.unwrap_or(0.0);

        let trend = if ema_s > ema_l {
            TrendDirection::Bullish
        } else {
            TrendDirection::Bearish
        };

        Some((trend, adx))
    }

    // ========================================================================
    // Position management (internal)
    // ========================================================================

    fn enter_position(&mut self, side: MultiTfSignal, price: f64, stop: f64, tp: f64) {
        self.in_position = true;
        self.entry_price = Some(price);
        self.entry_side = Some(side);
        self.stop_loss = Some(stop);
        self.take_profit = Some(tp);
    }

    fn check_exit(
        &self,
        close: f64,
        ema_long: f64,
        _atr: f64,
        side: &MultiTfSignal,
    ) -> Option<MultiTfSignal> {
        match side {
            MultiTfSignal::Buy => {
                // Exit long if price closes below 200 EMA (major trend break)
                if close < ema_long {
                    return Some(MultiTfSignal::Sell);
                }
                // Stop loss
                if let Some(stop) = self.stop_loss
                    && close <= stop
                {
                    return Some(MultiTfSignal::Sell);
                }
                // Take profit
                if let Some(tp) = self.take_profit
                    && close >= tp
                {
                    return Some(MultiTfSignal::Sell);
                }
                None
            }
            MultiTfSignal::Sell => {
                // Exit short if price closes above 200 EMA
                if close > ema_long {
                    return Some(MultiTfSignal::Buy);
                }
                if let Some(stop) = self.stop_loss
                    && close >= stop
                {
                    return Some(MultiTfSignal::Buy);
                }
                if let Some(tp) = self.take_profit
                    && close <= tp
                {
                    return Some(MultiTfSignal::Buy);
                }
                None
            }
            MultiTfSignal::Hold => None,
        }
    }

    fn exit_position(&mut self) {
        self.in_position = false;
        self.entry_price = None;
        self.entry_side = None;
        self.stop_loss = None;
        self.take_profit = None;
    }

    fn make_result(&self, signal: MultiTfSignal, confidence: f64, reason: String) -> MultiTfResult {
        let ltf_trend = match (self.last_ema_short, self.last_ema_long) {
            (Some(s), Some(l)) if s > l => Some(TrendDirection::Bullish),
            (Some(s), Some(l)) if s < l => Some(TrendDirection::Bearish),
            _ => None,
        };

        MultiTfResult {
            signal,
            ema_short: self.last_ema_short,
            ema_long: self.last_ema_long,
            adx: self.last_adx,
            atr: self.last_atr,
            ltf_trend,
            htf_trend: self.htf_trend,
            confidence,
            stop_loss: if matches!(signal, MultiTfSignal::Buy | MultiTfSignal::Sell) {
                self.stop_loss
            } else {
                None
            },
            take_profit: if matches!(signal, MultiTfSignal::Buy | MultiTfSignal::Sell) {
                self.take_profit
            } else {
                None
            },
            reason,
        }
    }

    // ========================================================================
    // Reset
    // ========================================================================

    /// Full reset — clears all indicator and position state.
    pub fn reset(&mut self) {
        self.ema_short = EMA::new(self.config.ema_short_period);
        self.ema_long = EMA::new(self.config.ema_long_period);
        self.adx = ADX::new(self.config.adx_period);
        self.atr = ATR::new(self.config.atr_period);
        self.exit_position();
        self.last_ema_short = None;
        self.last_ema_long = None;
        self.last_adx = None;
        self.last_atr = None;
        self.last_signal = MultiTfSignal::Hold;
        self.candle_count = 0;
        self.htf_trend = None;
    }

    // ========================================================================
    // Accessors
    // ========================================================================

    /// Whether the strategy has enough data to produce signals.
    pub fn is_ready(&self) -> bool {
        self.candle_count >= self.config.warmup_candles
            && self.last_ema_short.is_some()
            && self.last_ema_long.is_some()
            && self.last_atr.is_some()
    }

    /// Whether the strategy currently has an open position.
    pub fn is_in_position(&self) -> bool {
        self.in_position
    }

    /// The last short EMA value (50-period).
    pub fn last_ema_short(&self) -> Option<f64> {
        self.last_ema_short
    }

    /// The last long EMA value (200-period).
    pub fn last_ema_long(&self) -> Option<f64> {
        self.last_ema_long
    }

    /// The last ADX value.
    pub fn last_adx(&self) -> Option<f64> {
        self.last_adx
    }

    /// The last ATR value.
    pub fn last_atr(&self) -> Option<f64> {
        self.last_atr
    }

    /// The last signal produced.
    pub fn last_signal(&self) -> MultiTfSignal {
        self.last_signal
    }

    /// Total candles processed.
    pub fn candle_count(&self) -> usize {
        self.candle_count
    }

    /// Current entry price (if in position).
    pub fn entry_price(&self) -> Option<f64> {
        self.entry_price
    }

    /// Current stop loss (if in position).
    pub fn stop_loss(&self) -> Option<f64> {
        self.stop_loss
    }

    /// Current take profit (if in position).
    pub fn take_profit(&self) -> Option<f64> {
        self.take_profit
    }

    /// Strategy configuration.
    pub fn config(&self) -> &MultiTfConfig {
        &self.config
    }

    /// Current HTF trend bias (if set).
    pub fn htf_trend(&self) -> Option<TrendDirection> {
        self.htf_trend
    }

    /// Detected LTF trend from EMAs.
    pub fn detected_ltf_trend(&self) -> Option<TrendDirection> {
        match (self.last_ema_short, self.last_ema_long) {
            (Some(s), Some(l)) if s > l => Some(TrendDirection::Bullish),
            (Some(s), Some(l)) if s < l => Some(TrendDirection::Bearish),
            _ => None,
        }
    }
}

