/// Detailed result from a Multi-Timeframe Trend strategy update.
#[derive(Debug, Clone)]
pub struct MultiTfResult {
    /// The signal produced.
    pub signal: MultiTfSignal,
    /// Current short EMA value (50-period).
    pub ema_short: Option<f64>,
    /// Current long EMA value (200-period).
    pub ema_long: Option<f64>,
    /// Current ADX value.
    pub adx: Option<f64>,
    /// Current ATR value.
    pub atr: Option<f64>,
    /// Detected lower-timeframe trend direction.
    pub ltf_trend: Option<TrendDirection>,
    /// HTF trend bias (externally set).
    pub htf_trend: Option<TrendDirection>,
    /// Confidence score (0.0–1.0).
    pub confidence: f64,
    /// Computed stop-loss price.
    pub stop_loss: Option<f64>,
    /// Computed take-profit price.
    pub take_profit: Option<f64>,
    /// Human-readable reason string.
    pub reason: String,
}

