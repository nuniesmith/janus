/// Configuration for the EMA Ribbon Scalper strategy.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct EmaRibbonConfig {
    /// Fast EMA period (default: 8).
    pub fast_ema_period: usize,

    /// Mid EMA period (default: 13).
    pub mid_ema_period: usize,

    /// Slow EMA period (default: 21).
    pub slow_ema_period: usize,

    /// ATR period for volatility measurement (default: 14).
    pub atr_period: usize,

    /// ATR multiplier for stop-loss distance (default: 1.5).
    pub atr_stop_multiplier: f64,

    /// ATR multiplier for take-profit distance (default: 3.0).
    pub atr_tp_multiplier: f64,

    /// Rolling volume lookback period for spike detection (default: 20).
    pub volume_period: usize,

    /// Minimum volume multiplier (vs rolling average) to confirm entry
    /// (default: 1.3). Set lower than VWAP since trend context provides
    /// additional confirmation.
    pub min_volume_multiplier: f64,

    /// Minimum confidence score to emit a signal (default: 0.55).
    pub min_confidence: f64,

    /// Minimum candle body ratio (body / range) to count as "strong" candle
    /// (default: 0.45).
    pub min_body_ratio: f64,

    /// Pullback tolerance — how far past the mid EMA the price can go, as a
    /// fraction (default: 0.002 = 0.2%). For a long setup, `price <= mid_ema * (1 + tolerance)`.
    pub pullback_tolerance: f64,

    /// Minimum number of candles before the strategy is ready (default: 25).
    pub warmup_candles: usize,
}

impl Default for EmaRibbonConfig {
    fn default() -> Self {
        Self {
            fast_ema_period: 8,
            mid_ema_period: 13,
            slow_ema_period: 21,
            atr_period: 14,
            atr_stop_multiplier: 1.5,
            atr_tp_multiplier: 3.0,
            volume_period: 20,
            min_volume_multiplier: 1.3,
            min_confidence: 0.55,
            min_body_ratio: 0.45,
            pullback_tolerance: 0.002,
            warmup_candles: 25,
        }
    }
}

impl EmaRibbonConfig {
    /// Aggressive config for crypto — tighter tolerance, lower thresholds.
    pub fn crypto_aggressive() -> Self {
        Self {
            atr_stop_multiplier: 1.2,
            atr_tp_multiplier: 2.5,
            min_volume_multiplier: 1.2,
            min_confidence: 0.50,
            min_body_ratio: 0.40,
            pullback_tolerance: 0.003,
            warmup_candles: 20,
            ..Default::default()
        }
    }

    /// Conservative config — wider stops, higher confidence required.
    pub fn conservative() -> Self {
        Self {
            atr_stop_multiplier: 2.0,
            atr_tp_multiplier: 4.0,
            min_volume_multiplier: 1.5,
            min_confidence: 0.65,
            min_body_ratio: 0.50,
            pullback_tolerance: 0.001,
            warmup_candles: 30,
            ..Default::default()
        }
    }
}

