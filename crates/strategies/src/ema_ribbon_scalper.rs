    // ── Signal generation tests ──────────────────────────────────────────

    #[test]
    fn test_buy_signal_on_pullback() {
        let mut strategy = EmaRibbonScalperStrategy::new(EmaRibbonConfig {
            min_confidence: 0.40,
            min_volume_multiplier: 1.0,
            warmup_candles: 25,
            ..EmaRibbonConfig::default()
        });

        // Build strong uptrend to align ribbon
        for i in 0..40 {
            let price = 50000.0 + (i as f64) * 30.0;
            strategy.update_ohlcv(price - 5.0, price + 15.0, price - 15.0, price, 1000.0);
        }

        assert!(strategy.is_ribbon_bullish());

        // Now simulate a pullback to mid EMA area
        if let Some(mid) = strategy.last_mid_ema() {
            // Drop price to just at the mid EMA but above slow EMA
            let slow = strategy.last_slow_ema().unwrap();
            let pullback_price = mid; // right at mid EMA
            // Ensure close > slow
            if pullback_price > slow {
                let result = strategy.update_ohlcv(
                    pullback_price - 10.0, // open lower
                    pullback_price + 20.0, // high above
                    pullback_price - 20.0, // low below
                    pullback_price + 5.0,  // close bullish above open
                    3000.0,                // 3x normal volume
                );
                // May or may not fire depending on exact EMA alignment after the dip
                // but should not panic
                assert!(result.fast_ema.is_some());
                assert!(result.mid_ema.is_some());
                assert!(result.slow_ema.is_some());
            }
        }
    }

