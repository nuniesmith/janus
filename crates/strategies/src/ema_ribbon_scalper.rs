//! # EMA Ribbon Scalper Strategy
//!
//! Uses an 8/13/21 EMA ribbon to identify trend direction and enters on
//! pullbacks to the ribbon with volume confirmation. ATR-based stop losses
//! and take profits.
//!
//! ## How It Works
//!
//! 1. **Ribbon Direction**: All three EMAs (8, 13, 21) are checked for alignment.
//!    Bullish when 8 > 13 > 21, bearish when 8 < 13 < 21.
//!
//! 2. **Pullback Detection**: Price pulls back to touch or cross the middle EMA
//!    (13) but remains on the correct side of the slow EMA (21).
//!
//! 3. **Volume Confirmation**: Volume must be above a configurable multiple of
//!    the rolling average to confirm institutional interest.
//!
//! 4. **Candle Confirmation**: The entry candle must be directionally aligned
//!    (bullish candle for long, bearish for short) with a strong body.
//!
//! 5. **ATR-Based Stops**: Stop loss and take profit are placed at ATR multiples
//!    from entry.
//!
//! ## Regime Integration
//!
//! This strategy is designed for trending markets. Use `should_trade_in_regime()`
//! to gate activation:
//!
//! - **Trending(Bullish/Bearish)** → Yes (primary use case)
//! - **Volatile** → Yes with reduced size (momentum may carry)
//! - **MeanReverting** → No (ribbon signals are noise in ranging markets)
//! - **Uncertain** → No (wait for clarity)
//!
//! ## Origin
//!
//! Ported from Kraken's `strategy/scalping.rs::EmaRibbonScalper`, adapted for
//! the JANUS single-asset design, `janus-regime` indicator types, and
//! regime-aware position sizing.

use janus_regime::MarketRegime;
use janus_regime::indicators::{ATR, EMA};
use janus_regime::types::TrendDirection;
use serde::{Deserialize, Serialize};

// ============================================================================
// Signal Types
