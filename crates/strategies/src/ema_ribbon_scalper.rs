/// EMA Ribbon Scalper Strategy
///
/// Trend-following scalper using 8/13/21 EMA ribbon alignment with pullback
/// entries. Single-asset — instantiate one per symbol.
#[derive(Debug)]
pub struct EmaRibbonScalperStrategy {
    config: EmaRibbonConfig,

    // Indicators
    fast_ema: EMA,
    mid_ema: EMA,
    slow_ema: EMA,
    atr: ATR,
    volume_analyzer: VolumeAnalyzer,

    // Position tracking
    in_position: bool,
    entry_price: Option<f64>,
    entry_side: Option<EmaRibbonSignal>,
    stop_loss: Option<f64>,
    take_profit: Option<f64>,

    // Last computed values
    last_fast_ema: Option<f64>,
    last_mid_ema: Option<f64>,
    last_slow_ema: Option<f64>,
    last_atr: Option<f64>,
    last_signal: EmaRibbonSignal,
    candle_count: usize,

    // HTF trend bias (optional — set externally from higher-timeframe analysis)
    htf_trend: Option<TrendDirection>,
}

impl EmaRibbonScalperStrategy {
    /// Create a new EMA Ribbon Scalper strategy with the given config.
    pub fn new(config: EmaRibbonConfig) -> Self {
        let fast_ema = EMA::new(config.fast_ema_period);
        let mid_ema = EMA::new(config.mid_ema_period);
        let slow_ema = EMA::new(config.slow_ema_period);
        let atr = ATR::new(config.atr_period);
        let volume_analyzer = VolumeAnalyzer::new(config.volume_period);

        Self {
            fast_ema,
            mid_ema,
            slow_ema,
            atr,
            volume_analyzer,
            config,
            in_position: false,
            entry_price: None,
            entry_side: None,
            stop_loss: None,
            take_profit: None,
            last_fast_ema: None,
            last_mid_ema: None,
            last_slow_ema: None,
            last_atr: None,
            last_signal: EmaRibbonSignal::Hold,
            candle_count: 0,
            htf_trend: None,
        }
    }

    /// Create with default configuration.
    pub fn with_defaults() -> Self {
        Self::new(EmaRibbonConfig::default())
    }

    /// Set higher-timeframe trend bias. When set, long setups are only taken
    /// if HTF is bullish (or flat), and short setups only if HTF is bearish
    /// (or flat).
    pub fn set_htf_trend(&mut self, trend: TrendDirection) {
        self.htf_trend = Some(trend);
    }

    /// Clear the HTF trend bias.
    pub fn clear_htf_trend(&mut self) {
        self.htf_trend = None;
    }

    /// Update with a `janus-models` Candle.
    pub fn update_candle(&mut self, candle: &janus_models::Candle) -> EmaRibbonSignal {
        self.update_ohlcv(
            candle.open,
            candle.high,
            candle.low,
            candle.close,
            candle.volume,
        )
        .signal
    }

    /// Update with a `janus-models` Candle and return detailed result.
    pub fn update_candle_with_reason(&mut self, candle: &janus_models::Candle) -> EmaRibbonResult {
        self.update_ohlcv(
            candle.open,
            candle.high,
            candle.low,
            candle.close,
            candle.volume,
        )
    }

    /// Main update with OHLCV values — returns a detailed result.
    pub fn update_ohlcv(
        &mut self,
        open: f64,
        high: f64,
        low: f64,
        close: f64,
        volume: f64,
    ) -> EmaRibbonResult {
        self.candle_count += 1;

        // Update indicators
        self.fast_ema.update(close);
        self.mid_ema.update(close);
        self.slow_ema.update(close);
        self.atr.update(high, low, close);
        let vol_mult = self.volume_analyzer.update(volume);

        self.last_fast_ema = self.fast_ema.value();
        self.last_mid_ema = self.mid_ema.value();
        self.last_slow_ema = self.slow_ema.value();
        self.last_atr = self.atr.value();

        // Not ready yet?
        if self.candle_count < self.config.warmup_candles {
            return self.make_result(
                EmaRibbonSignal::Hold,
                vol_mult,
                0.0,
                "Warming up".to_string(),
            );
        }

        let fast = match self.last_fast_ema {
            Some(v) => v,
            None => {
                return self.make_result(
                    EmaRibbonSignal::Hold,
                    vol_mult,
                    0.0,
                    "Fast EMA not ready".to_string(),
                );
            }
        };
        let mid = match self.last_mid_ema {
            Some(v) => v,
            None => {
                return self.make_result(
                    EmaRibbonSignal::Hold,
                    vol_mult,
                    0.0,
                    "Mid EMA not ready".to_string(),
                );
            }
        };
        let slow = match self.last_slow_ema {
            Some(v) => v,
            None => {
                return self.make_result(
                    EmaRibbonSignal::Hold,
                    vol_mult,
                    0.0,
                    "Slow EMA not ready".to_string(),
                );
            }
        };

        let atr_val = match self.last_atr {
            Some(a) if a > 0.0 => a,
            _ => {
                return self.make_result(
                    EmaRibbonSignal::Hold,
                    vol_mult,
                    0.0,
                    "ATR not ready".to_string(),
                );
            }
        };

        // Ribbon alignment
        let ribbon_bullish = fast > mid && mid > slow;
        let ribbon_bearish = fast < mid && mid < slow;

        // Candle properties
        let body = (close - open).abs();
        let range = high - low;
        let is_bullish = close > open;
        let is_bearish = close < open;
        let body_ratio = if range > 0.0 { body / range } else { 0.0 };
        let upper_wick = high - close.max(open);
        let lower_wick = close.min(open) - low;

        // Check for exit if in position
        if self.in_position
            && let Some(ref side) = self.entry_side
        {
            let exit = self.check_exit(close, fast, slow, atr_val, side);
            if let Some(exit_signal) = exit {
                self.exit_position();
                return self.make_result(
                    exit_signal,
                    vol_mult,
                    0.8,
                    format!(
                        "Ribbon exit: price {:.2} (fast={:.2}, slow={:.2})",
                        close, fast, slow
                    ),
                );
            }
        }

        // Already in position — don't open another
        if self.in_position {
            return self.make_result(
                EmaRibbonSignal::Hold,
                vol_mult,
                0.0,
                "In position".to_string(),
            );
        }

        // HTF trend filtering
        let htf_allows_long = !matches!(self.htf_trend, Some(TrendDirection::Bearish));
        let htf_allows_short = !matches!(self.htf_trend, Some(TrendDirection::Bullish));

        // === LONG SETUP ===
        // Ribbon bullish + price pulled back to mid EMA + bullish candle + volume
        if ribbon_bullish
            && htf_allows_long
            && close > slow
            && close <= mid * (1.0 + self.config.pullback_tolerance)
            && is_bullish
            && vol_mult >= self.config.min_volume_multiplier
        {
            let mut confidence: f64 = 0.40;

            // HTF alignment bonus
            if self.htf_trend == Some(TrendDirection::Bullish) {
                confidence += 0.20;
            }

            // Volume spike bonus
            if vol_mult > 2.0 {
                confidence += 0.15;
            } else if vol_mult >= self.config.min_volume_multiplier {
                confidence += 0.05;
            }

            // Strong candle body bonus
            if body_ratio >= self.config.min_body_ratio {
                confidence += 0.10;
            }

            // Body larger than wicks — clean candle
            if body > upper_wick + lower_wick {
                confidence += 0.05;
            }

            // Tight ribbon (EMAs close together = strong trend)
            let ribbon_spread = ((fast - slow) / slow).abs();
            if ribbon_spread < 0.01 {
                confidence += 0.05;
            }

            confidence = confidence.min(1.0);

            if confidence >= self.config.min_confidence {
                let stop = close - atr_val * self.config.atr_stop_multiplier;
                let tp = close + atr_val * self.config.atr_tp_multiplier;

                self.enter_position(EmaRibbonSignal::Buy, close, stop, tp);
                self.last_signal = EmaRibbonSignal::Buy;

                return self.make_result(
                    EmaRibbonSignal::Buy,
                    vol_mult,
                    confidence,
                    format!(
                        "Ribbon long: price {:.2} pullback to mid EMA {:.2}, ribbon [{:.2}/{:.2}/{:.2}], vol {:.1}x",
                        close, mid, fast, mid, slow, vol_mult
                    ),
                );
            }
        }

        // === SHORT SETUP ===
        // Ribbon bearish + price pulled back to mid EMA + bearish candle + volume
        if ribbon_bearish
            && htf_allows_short
            && close < slow
            && close >= mid * (1.0 - self.config.pullback_tolerance)
            && is_bearish
            && vol_mult >= self.config.min_volume_multiplier
        {
            let mut confidence: f64 = 0.40;

            // HTF alignment bonus
            if self.htf_trend == Some(TrendDirection::Bearish) {
                confidence += 0.20;
            }

            // Volume spike bonus
            if vol_mult > 2.0 {
                confidence += 0.15;
            } else if vol_mult >= self.config.min_volume_multiplier {
                confidence += 0.05;
            }

            // Strong candle body bonus
            if body_ratio >= self.config.min_body_ratio {
                confidence += 0.10;
            }

            // Body larger than wicks
            if body > upper_wick + lower_wick {
                confidence += 0.05;
            }

            // Tight ribbon
            let ribbon_spread = ((fast - slow) / slow).abs();
            if ribbon_spread < 0.01 {
                confidence += 0.05;
            }

            confidence = confidence.min(1.0);

            if confidence >= self.config.min_confidence {
                let stop = close + atr_val * self.config.atr_stop_multiplier;
                let tp = close - atr_val * self.config.atr_tp_multiplier;

                self.enter_position(EmaRibbonSignal::Sell, close, stop, tp);
                self.last_signal = EmaRibbonSignal::Sell;

                return self.make_result(
                    EmaRibbonSignal::Sell,
                    vol_mult,
                    confidence,
                    format!(
                        "Ribbon short: price {:.2} pullback to mid EMA {:.2}, ribbon [{:.2}/{:.2}/{:.2}], vol {:.1}x",
                        close, mid, fast, mid, slow, vol_mult
                    ),
                );
            }
        }

        self.last_signal = EmaRibbonSignal::Hold;
        self.make_result(
            EmaRibbonSignal::Hold,
            vol_mult,
            0.0,
            format!(
                "No signal: price {:.2}, ribbon [{:.2}/{:.2}/{:.2}], {}",
                close,
                fast,
                mid,
                slow,
                if ribbon_bullish {
                    "bullish"
                } else if ribbon_bearish {
                    "bearish"
                } else {
                    "mixed"
                }
            ),
        )
    }

    /// Update with HLC values (no volume — uses volume=0 fallback).
    pub fn update_hlc(&mut self, high: f64, low: f64, close: f64) -> EmaRibbonResult {
        self.update_ohlcv(close, high, low, close, 0.0)
    }

    // ========================================================================
    // Regime gating
    // ========================================================================

    /// Check whether this strategy should trade in the given market regime.
    ///
    /// EMA Ribbon Scalper is a trend-following strategy — it works best when
    /// the market has a clear directional bias.
    pub fn should_trade_in_regime(regime: &MarketRegime) -> bool {
        matches!(regime, MarketRegime::Trending(_) | MarketRegime::Volatile)
    }

    /// Compute a confidence-weighted position size factor for the given regime.
    ///
    /// Returns `Some(factor)` if the regime is suitable, `None` otherwise.
    pub fn regime_size_factor(regime: &MarketRegime, confidence: f64) -> Option<f64> {
        match regime {
            MarketRegime::Trending(_) => {
                // Primary regime — full confidence scaling
                Some(confidence.clamp(0.4, 1.0))
            }
            MarketRegime::Volatile => {
                // Momentum can carry in volatile markets, but reduce size
                Some(if confidence >= 0.6 { 0.40 } else { 0.25 })
            }
            _ => None,
        }
    }

    // ========================================================================
    // Position management (internal)
    // ========================================================================

    fn enter_position(&mut self, side: EmaRibbonSignal, price: f64, stop: f64, tp: f64) {
        self.in_position = true;
        self.entry_price = Some(price);
        self.entry_side = Some(side);
        self.stop_loss = Some(stop);
        self.take_profit = Some(tp);
    }

    fn check_exit(
        &self,
        close: f64,
        fast_ema: f64,
        slow_ema: f64,
        _atr: f64,
        side: &EmaRibbonSignal,
    ) -> Option<EmaRibbonSignal> {
        match side {
            EmaRibbonSignal::Buy => {
                // Exit long if price closes below slow EMA (trend break)
                if close < slow_ema {
                    return Some(EmaRibbonSignal::Sell);
                }
                // Exit long if fast EMA crosses below slow EMA
                if fast_ema < slow_ema {
                    return Some(EmaRibbonSignal::Sell);
                }
                // Check stop loss
                if let Some(stop) = self.stop_loss
                    && close <= stop
                {
                    return Some(EmaRibbonSignal::Sell);
                }
                // Check take profit
                if let Some(tp) = self.take_profit
                    && close >= tp
                {
                    return Some(EmaRibbonSignal::Sell);
                }
                None
            }
            EmaRibbonSignal::Sell => {
                // Exit short if price closes above slow EMA
                if close > slow_ema {
                    return Some(EmaRibbonSignal::Buy);
                }
                // Exit short if fast EMA crosses above slow EMA
                if fast_ema > slow_ema {
                    return Some(EmaRibbonSignal::Buy);
                }
                // Check stop loss
                if let Some(stop) = self.stop_loss
                    && close >= stop
                {
                    return Some(EmaRibbonSignal::Buy);
                }
                // Check take profit
                if let Some(tp) = self.take_profit
                    && close <= tp
                {
                    return Some(EmaRibbonSignal::Buy);
                }
                None
            }
            EmaRibbonSignal::Hold => None,
        }
    }

    fn exit_position(&mut self) {
        self.in_position = false;
        self.entry_price = None;
        self.entry_side = None;
        self.stop_loss = None;
        self.take_profit = None;
    }

    fn make_result(
        &self,
        signal: EmaRibbonSignal,
        volume_multiplier: f64,
        confidence: f64,
        reason: String,
    ) -> EmaRibbonResult {
        EmaRibbonResult {
            signal,
            fast_ema: self.last_fast_ema,
            mid_ema: self.last_mid_ema,
            slow_ema: self.last_slow_ema,
            ribbon_bullish: self.is_ribbon_bullish(),
            ribbon_bearish: self.is_ribbon_bearish(),
            atr: self.last_atr,
            volume_multiplier,
            confidence,
            stop_loss: if matches!(signal, EmaRibbonSignal::Buy | EmaRibbonSignal::Sell) {
                self.stop_loss
            } else {
                None
            },
            take_profit: if matches!(signal, EmaRibbonSignal::Buy | EmaRibbonSignal::Sell) {
                self.take_profit
            } else {
                None
            },
            reason,
        }
    }

    // ========================================================================
    // Reset
    // ========================================================================

    /// Full reset — clears all indicator and position state.
    pub fn reset(&mut self) {
        self.fast_ema = EMA::new(self.config.fast_ema_period);
        self.mid_ema = EMA::new(self.config.mid_ema_period);
        self.slow_ema = EMA::new(self.config.slow_ema_period);
        self.atr = ATR::new(self.config.atr_period);
        self.volume_analyzer = VolumeAnalyzer::new(self.config.volume_period);
        self.exit_position();
        self.last_fast_ema = None;
        self.last_mid_ema = None;
        self.last_slow_ema = None;
        self.last_atr = None;
        self.last_signal = EmaRibbonSignal::Hold;
        self.candle_count = 0;
        self.htf_trend = None;
    }

    // ========================================================================
    // Accessors
    // ========================================================================

    /// Whether the strategy has enough data to produce signals.
    pub fn is_ready(&self) -> bool {
        self.candle_count >= self.config.warmup_candles
            && self.last_fast_ema.is_some()
            && self.last_mid_ema.is_some()
            && self.last_slow_ema.is_some()
            && self.last_atr.is_some()
    }

    /// Whether the ribbon is aligned bullish (fast > mid > slow).
    pub fn is_ribbon_bullish(&self) -> bool {
        match (self.last_fast_ema, self.last_mid_ema, self.last_slow_ema) {
            (Some(f), Some(m), Some(s)) => f > m && m > s,
            _ => false,
        }
    }

    /// Whether the ribbon is aligned bearish (fast < mid < slow).
    pub fn is_ribbon_bearish(&self) -> bool {
        match (self.last_fast_ema, self.last_mid_ema, self.last_slow_ema) {
            (Some(f), Some(m), Some(s)) => f < m && m < s,
            _ => false,
        }
    }

    /// Whether the strategy currently has an open position.
    pub fn is_in_position(&self) -> bool {
        self.in_position
    }

    /// The last fast EMA value.
    pub fn last_fast_ema(&self) -> Option<f64> {
        self.last_fast_ema
    }

    /// The last mid EMA value.
    pub fn last_mid_ema(&self) -> Option<f64> {
        self.last_mid_ema
    }

    /// The last slow EMA value.
    pub fn last_slow_ema(&self) -> Option<f64> {
        self.last_slow_ema
    }

    /// The last ATR value.
    pub fn last_atr(&self) -> Option<f64> {
        self.last_atr
    }

    /// The last signal produced.
    pub fn last_signal(&self) -> EmaRibbonSignal {
        self.last_signal
    }

    /// Total candles processed.
    pub fn candle_count(&self) -> usize {
        self.candle_count
    }

    /// Current entry price (if in position).
    pub fn entry_price(&self) -> Option<f64> {
        self.entry_price
    }

    /// Current stop loss (if in position).
    pub fn stop_loss(&self) -> Option<f64> {
        self.stop_loss
    }

    /// Current take profit (if in position).
    pub fn take_profit(&self) -> Option<f64> {
        self.take_profit
    }

    /// Strategy configuration.
    pub fn config(&self) -> &EmaRibbonConfig {
        &self.config
    }

    /// Current HTF trend bias (if set).
    pub fn htf_trend(&self) -> Option<TrendDirection> {
        self.htf_trend
    }

    /// Return current EMA ribbon values as (fast, mid, slow).
    pub fn ribbon_values(&self) -> (Option<f64>, Option<f64>, Option<f64>) {
        (self.last_fast_ema, self.last_mid_ema, self.last_slow_ema)
    }
}

