/// Detailed result from an EMA Ribbon Scalper strategy update.
#[derive(Debug, Clone)]
pub struct EmaRibbonResult {
    /// The signal produced.
    pub signal: EmaRibbonSignal,
    /// Current fast EMA value (if available).
    pub fast_ema: Option<f64>,
    /// Current mid EMA value (if available).
    pub mid_ema: Option<f64>,
    /// Current slow EMA value (if available).
    pub slow_ema: Option<f64>,
    /// Whether the ribbon is aligned bullish (fast > mid > slow).
    pub ribbon_bullish: bool,
    /// Whether the ribbon is aligned bearish (fast < mid < slow).
    pub ribbon_bearish: bool,
    /// Current ATR value (if available).
    pub atr: Option<f64>,
    /// Volume multiplier relative to rolling average.
    pub volume_multiplier: f64,
    /// Confidence score (0.0–1.0).
    pub confidence: f64,
    /// Computed stop-loss price (only set on buy/sell signals).
    pub stop_loss: Option<f64>,
    /// Computed take-profit price (only set on buy/sell signals).
    pub take_profit: Option<f64>,
    /// Human-readable reason string.
    pub reason: String,
}

// ============================================================================
// Volume Analyzer (internal)
// ============================================================================

/// Rolling volume analyzer for spike detection.
#[derive(Debug)]
struct VolumeAnalyzer {
    period: usize,
    volumes: Vec<f64>,
}

impl VolumeAnalyzer {
    fn new(period: usize) -> Self {
        Self {
            period: period.max(1),
            volumes: Vec::with_capacity(period),
        }
    }

    /// Push a new volume value. Returns the multiplier (volume / rolling avg).
    fn update(&mut self, volume: f64) -> f64 {
        let avg = self.average();

        self.volumes.push(volume);
        if self.volumes.len() > self.period {
            self.volumes.remove(0);
        }

        if avg > 0.0 { volume / avg } else { 1.0 }
    }

    /// Rolling average volume.
    fn average(&self) -> f64 {
        if self.volumes.is_empty() {
            0.0
        } else {
            self.volumes.iter().sum::<f64>() / self.volumes.len() as f64
        }
    }
}

