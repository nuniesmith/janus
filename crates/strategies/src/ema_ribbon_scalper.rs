/// Signal generated by the EMA Ribbon Scalper strategy.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum EmaRibbonSignal {
    /// Enter long — bullish ribbon pullback with volume.
    Buy,
    /// Enter short / exit long — bearish ribbon pullback with volume.
    Sell,
    /// No action.
    Hold,
}

impl std::fmt::Display for EmaRibbonSignal {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            EmaRibbonSignal::Buy => write!(f, "BUY"),
            EmaRibbonSignal::Sell => write!(f, "SELL"),
            EmaRibbonSignal::Hold => write!(f, "HOLD"),
        }
    }
}

