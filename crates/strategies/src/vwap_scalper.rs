/// VWAP Scalper Strategy
///
/// Mean-reversion scalping around VWAP bands. Single-asset — instantiate one
/// per symbol.
#[derive(Debug)]
pub struct VwapScalperStrategy {
    config: VwapScalperConfig,

    // Indicators
    vwap: VwapCalculator,
    atr: ATR,
    volume_analyzer: VolumeAnalyzer,

    // Position tracking
    in_position: bool,
    entry_price: Option<f64>,
    entry_side: Option<VwapSignal>,
    stop_loss: Option<f64>,
    take_profit: Option<f64>,

    // Last computed values
    last_vwap: Option<f64>,
    last_std_dev: Option<f64>,
    last_atr: Option<f64>,
    last_signal: VwapSignal,
    candle_count: usize,
}

impl VwapScalperStrategy {
    /// Create a new VWAP Scalper strategy with the given config.
    pub fn new(config: VwapScalperConfig) -> Self {
        let atr = ATR::new(config.atr_period);
        let volume_analyzer = VolumeAnalyzer::new(config.volume_period);

        Self {
            vwap: VwapCalculator::new(),
            atr,
            volume_analyzer,
            config,
            in_position: false,
            entry_price: None,
            entry_side: None,
            stop_loss: None,
            take_profit: None,
            last_vwap: None,
            last_std_dev: None,
            last_atr: None,
            last_signal: VwapSignal::Hold,
            candle_count: 0,
        }
    }

    /// Create with default configuration.
    pub fn with_defaults() -> Self {
        Self::new(VwapScalperConfig::default())
    }

    /// Update the strategy with a `janus-models` Candle.
    pub fn update_candle(&mut self, candle: &janus_models::Candle) -> VwapSignal {
        self.update_ohlcv(
            candle.open,
            candle.high,
            candle.low,
            candle.close,
            candle.volume,
        )
        .signal
    }

    /// Update the strategy with a `janus-models` Candle and return detailed result.
    pub fn update_candle_with_reason(
        &mut self,
        candle: &janus_models::Candle,
    ) -> VwapScalperResult {
        self.update_ohlcv(
            candle.open,
            candle.high,
            candle.low,
            candle.close,
            candle.volume,
        )
    }

    /// Update with OHLCV values and return detailed result.
    pub fn update_ohlcv(
        &mut self,
        open: f64,
        high: f64,
        low: f64,
        close: f64,
        volume: f64,
    ) -> VwapScalperResult {
        self.candle_count += 1;

        // Update indicators
        let (vwap, std_dev) = self.vwap.update(high, low, close, volume);
        self.atr.update(high, low, close);
        let vol_mult = self.volume_analyzer.update(volume);

        self.last_vwap = Some(vwap);
        self.last_std_dev = Some(std_dev);
        self.last_atr = self.atr.value();

        // Not ready yet?
        if self.candle_count < self.config.warmup_candles || std_dev <= 0.0 {
            return self.make_result(VwapSignal::Hold, vol_mult, 0.0, "Warming up".to_string());
        }

        let atr_val = match self.atr.value() {
            Some(a) if a > 0.0 => a,
            _ => {
                return self.make_result(
                    VwapSignal::Hold,
                    vol_mult,
                    0.0,
                    "ATR not ready".to_string(),
                );
            }
        };

        let upper_band = vwap + self.config.vwap_std_dev * std_dev;
        let lower_band = vwap - self.config.vwap_std_dev * std_dev;

        // Candle properties
        let body = (close - open).abs();
        let range = high - low;
        let is_bullish = close > open;
        let is_bearish = close < open;
        let body_ratio = if range > 0.0 { body / range } else { 0.0 };
        let upper_wick = high - close.max(open);
        let lower_wick = close.min(open) - low;

        // Check for exit if in position
        if self.in_position
            && let Some(ref side) = self.entry_side
        {
            let exit = self.check_exit(close, vwap, atr_val, side);
            if let Some(exit_signal) = exit {
                self.exit_position();
                return self.make_result(
                    exit_signal,
                    vol_mult,
                    0.8,
                    format!(
                        "VWAP exit: price {:.2} reached target/stop (vwap={:.2})",
                        close, vwap
                    ),
                );
            }
        }

        // Already in position — don't open another
        if self.in_position {
            return self.make_result(VwapSignal::Hold, vol_mult, 0.0, "In position".to_string());
        }

        // === LONG SETUP (Mean Reversion from Lower Band) ===
        if close <= lower_band && is_bullish {
            let deviation = if std_dev > 0.0 {
                (vwap - close) / std_dev
            } else {
                0.0
            };

            let mut confidence = 0.40 + (deviation / 4.0).min(0.30);

            // Volume spike boost
            if vol_mult >= self.config.min_volume_multiplier {
                confidence += 0.10;
            }

            // Pin bar / hammer pattern: long lower wick
            if lower_wick > body * 2.0 {
                confidence += 0.15;
            }

            // Strong body
            if body_ratio >= self.config.min_body_ratio {
                confidence += 0.05;
            }

            confidence = confidence.min(1.0);

            if confidence >= self.config.min_confidence {
                let stop = close - atr_val * self.config.atr_stop_multiplier;
                let tp = vwap; // Target the mean

                self.enter_position(VwapSignal::Buy, close, stop, tp);
                self.last_signal = VwapSignal::Buy;

                return self.make_result(
                    VwapSignal::Buy,
                    vol_mult,
                    confidence,
                    format!(
                        "VWAP long: price {:.2} at lower band {:.2}, target VWAP {:.2}, dev {:.1}σ, vol {:.1}x",
                        close, lower_band, vwap, deviation, vol_mult
                    ),
                );
            }
        }

        // === SHORT SETUP (Mean Reversion from Upper Band) ===
        if close >= upper_band && is_bearish {
            let deviation = if std_dev > 0.0 {
                (close - vwap) / std_dev
            } else {
                0.0
            };

            let mut confidence = 0.40 + (deviation / 4.0).min(0.30);

            // Volume spike boost
            if vol_mult >= self.config.min_volume_multiplier {
                confidence += 0.10;
            }

            // Shooting star pattern: long upper wick
            if upper_wick > body * 2.0 {
                confidence += 0.15;
            }

            // Strong body
            if body_ratio >= self.config.min_body_ratio {
                confidence += 0.05;
            }

            confidence = confidence.min(1.0);

            if confidence >= self.config.min_confidence {
                let stop = close + atr_val * self.config.atr_stop_multiplier;
                let tp = vwap; // Target the mean

                self.enter_position(VwapSignal::Sell, close, stop, tp);
                self.last_signal = VwapSignal::Sell;

                return self.make_result(
                    VwapSignal::Sell,
                    vol_mult,
                    confidence,
                    format!(
                        "VWAP short: price {:.2} at upper band {:.2}, target VWAP {:.2}, dev {:.1}σ, vol {:.1}x",
                        close, upper_band, vwap, deviation, vol_mult
                    ),
                );
            }
        }

        self.last_signal = VwapSignal::Hold;
        self.make_result(
            VwapSignal::Hold,
            vol_mult,
            0.0,
            format!(
                "No signal: price {:.2}, VWAP {:.2}, bands [{:.2}, {:.2}]",
                close, vwap, lower_band, upper_band
            ),
        )
    }

    /// Update with HLC values (no volume — uses volume=1.0 fallback).
    pub fn update_hlc(&mut self, high: f64, low: f64, close: f64) -> VwapScalperResult {
        self.update_ohlcv(close, high, low, close, 0.0)
    }

    // ========================================================================
    // Regime gating
    // ========================================================================

    /// Check whether this strategy should trade in the given market regime.
    ///
    /// VWAP scalping is mean-reversion around the session average — it works
    /// best when the market oscillates around a stable mean.
    pub fn should_trade_in_regime(regime: &MarketRegime) -> bool {
        matches!(
            regime,
            MarketRegime::MeanReverting | MarketRegime::Uncertain
        )
    }

    /// Compute a confidence-weighted position size factor for the given regime.
    ///
    /// Returns `Some(factor)` if the regime is suitable, `None` otherwise.
    pub fn regime_size_factor(regime: &MarketRegime, confidence: f64) -> Option<f64> {
        match regime {
            MarketRegime::MeanReverting => {
                // Primary regime — full confidence scaling
                Some(confidence.clamp(0.4, 1.0))
            }
            MarketRegime::Uncertain => {
                // May be ranging — allow with reduced size
                Some(if confidence >= 0.5 { 0.30 } else { 0.20 })
            }
            _ => None,
        }
    }

    // ========================================================================
    // Session management
    // ========================================================================

    /// Reset VWAP for a new session. Call this at session boundaries
    /// (e.g. 00:00 UTC for crypto).
    pub fn reset_session(&mut self) {
        self.vwap.reset();
        self.last_vwap = None;
        self.last_std_dev = None;
        // Note: ATR and volume analyzer are NOT reset — they provide
        // cross-session context.
    }

    /// Full reset — clears all state including positions and indicators.
    pub fn reset(&mut self) {
        self.vwap.reset();
        self.atr.reset();
        self.volume_analyzer = VolumeAnalyzer::new(self.config.volume_period);
        self.in_position = false;
        self.entry_price = None;
        self.entry_side = None;
        self.stop_loss = None;
        self.take_profit = None;
        self.last_vwap = None;
        self.last_std_dev = None;
        self.last_atr = None;
        self.last_signal = VwapSignal::Hold;
        self.candle_count = 0;
    }

    // ========================================================================
    // Position management (internal)
    // ========================================================================

    fn enter_position(&mut self, side: VwapSignal, price: f64, stop: f64, tp: f64) {
        self.in_position = true;
        self.entry_price = Some(price);
        self.entry_side = Some(side);
        self.stop_loss = Some(stop);
        self.take_profit = Some(tp);
    }

    fn check_exit(
        &self,
        price: f64,
        vwap: f64,
        _atr: f64,
        side: &VwapSignal,
    ) -> Option<VwapSignal> {
        let stop = self.stop_loss.unwrap_or(0.0);
        let tp = self.take_profit.unwrap_or(vwap);

        match side {
            VwapSignal::Buy => {
                // Long position: exit at VWAP (TP) or below stop
                if price >= tp || price <= stop {
                    Some(VwapSignal::Sell)
                } else {
                    None
                }
            }
            VwapSignal::Sell => {
                // Short position: exit at VWAP (TP) or above stop
                if price <= tp || price >= stop {
                    Some(VwapSignal::Buy)
                } else {
                    None
                }
            }
            _ => None,
        }
    }

    fn exit_position(&mut self) {
        self.in_position = false;
        self.entry_price = None;
        self.entry_side = None;
        self.stop_loss = None;
        self.take_profit = None;
    }

    fn make_result(
        &self,
        signal: VwapSignal,
        volume_multiplier: f64,
        confidence: f64,
        reason: String,
    ) -> VwapScalperResult {
        let (upper, lower) = match (self.last_vwap, self.last_std_dev) {
            (Some(v), Some(s)) if s > 0.0 => (
                Some(v + self.config.vwap_std_dev * s),
                Some(v - self.config.vwap_std_dev * s),
            ),
            _ => (None, None),
        };

        VwapScalperResult {
            signal,
            vwap: self.last_vwap,
            upper_band: upper,
            lower_band: lower,
            atr: self.last_atr,
            volume_multiplier,
            confidence,
            stop_loss: if signal == VwapSignal::Buy || signal == VwapSignal::Sell {
                self.stop_loss
            } else {
                None
            },
            take_profit: if signal == VwapSignal::Buy || signal == VwapSignal::Sell {
                self.take_profit
            } else {
                None
            },
            reason,
        }
    }

    // ========================================================================
    // Accessors
    // ========================================================================

    /// Whether the strategy has processed enough candles to generate signals.
    pub fn is_ready(&self) -> bool {
        self.candle_count >= self.config.warmup_candles
    }

    /// Whether the strategy currently tracks an open position.
    pub fn is_in_position(&self) -> bool {
        self.in_position
    }

    /// Current VWAP value (if available).
    pub fn last_vwap(&self) -> Option<f64> {
        self.last_vwap
    }

    /// Current VWAP standard deviation (if available).
    pub fn last_std_dev(&self) -> Option<f64> {
        self.last_std_dev
    }

    /// Current ATR value (if available).
    pub fn last_atr(&self) -> Option<f64> {
        self.last_atr
    }

    /// Last signal produced.
    pub fn last_signal(&self) -> VwapSignal {
        self.last_signal
    }

    /// Number of candles processed since creation / last reset.
    pub fn candle_count(&self) -> usize {
        self.candle_count
    }

    /// Entry price of the current position (if any).
    pub fn entry_price(&self) -> Option<f64> {
        self.entry_price
    }

    /// Stop loss of the current position (if any).
    pub fn stop_loss(&self) -> Option<f64> {
        self.stop_loss
    }

    /// Take profit of the current position (if any).
    pub fn take_profit(&self) -> Option<f64> {
        self.take_profit
    }

    /// Reference to the strategy configuration.
    pub fn config(&self) -> &VwapScalperConfig {
        &self.config
    }

    /// Get current VWAP levels: `(vwap, lower_band, upper_band)`.
    pub fn vwap_levels(&self) -> Option<(f64, f64, f64)> {
        match (self.last_vwap, self.last_std_dev) {
            (Some(v), Some(s)) if s > 0.0 => {
                let upper = v + self.config.vwap_std_dev * s;
                let lower = v - self.config.vwap_std_dev * s;
                Some((v, lower, upper))
            }
            _ => None,
        }
    }
}

