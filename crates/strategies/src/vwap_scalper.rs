//! # VWAP Scalper Strategy
//!
//! Mean reversion scalping around the Volume-Weighted Average Price (VWAP)
//! with standard deviation bands. Enters long when price touches the lower
//! VWAP band with a bullish candle, short when it touches the upper band
//! with a bearish candle. Targets the VWAP (mean) for exits.
//!
//! ## How It Works
//!
//! 1. **VWAP Calculation**: Tracks cumulative typical-price × volume and
//!    cumulative volume to compute the session VWAP. Standard deviation
//!    bands are derived from the volume-weighted squared deviations.
//!
//! 2. **Band Touch Entry**: When price closes at or beyond a VWAP band,
//!    a mean-reversion entry is signaled. The assumption is that price
//!    will revert to the VWAP mean.
//!
//! 3. **Candle Confirmation**: Bullish candles at the lower band, bearish
//!    candles at the upper band. Pin-bar / hammer patterns add confidence.
//!
//! 4. **ATR-Based Stops**: Stop loss placed at an ATR multiple beyond the
//!    entry. Take profit targets the VWAP (the mean).
//!
//! 5. **Volume Spike Detection**: A rolling volume analyzer detects unusual
//!    volume, which boosts signal confidence.
//!
//! ## Regime Integration
//!
//! VWAP scalping is a mean-reversion strategy and works best when the market
//! is ranging. Use `should_trade_in_regime()` to gate activation:
//!
//! - **MeanReverting** → Yes (primary operating regime)
//! - **Uncertain** → Yes with reduced size (may be ranging)
//! - **Trending** → No (VWAP will lag badly in trends)
//! - **Volatile** → No (bands widen too much, stops get hit)
//!
//! ## Origin
//!
//! Ported from Kraken's `strategy/scalping.rs::VwapScalper`, adapted for the
//! JANUS single-asset design, `janus-regime` indicator types, and regime-aware
//! position sizing.

