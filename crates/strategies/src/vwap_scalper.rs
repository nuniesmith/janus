/// Signal generated by the VWAP Scalper strategy.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum VwapSignal {
    /// Enter long — price at/below lower VWAP band with bullish confirmation.
    Buy,
    /// Enter short / exit long — price at/above upper VWAP band with bearish confirmation.
    Sell,
    /// No action.
    Hold,
}

impl std::fmt::Display for VwapSignal {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            VwapSignal::Buy => write!(f, "BUY"),
            VwapSignal::Sell => write!(f, "SELL"),
            VwapSignal::Hold => write!(f, "HOLD"),
        }
    }
}

