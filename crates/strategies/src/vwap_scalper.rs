/// Detailed result from a VWAP Scalper strategy update.
#[derive(Debug, Clone)]
pub struct VwapScalperResult {
    /// The signal produced.
    pub signal: VwapSignal,
    /// Current VWAP value (if available).
    pub vwap: Option<f64>,
    /// Upper VWAP band (vwap + std_dev * multiplier).
    pub upper_band: Option<f64>,
    /// Lower VWAP band (vwap - std_dev * multiplier).
    pub lower_band: Option<f64>,
    /// Current ATR value (if available).
    pub atr: Option<f64>,
    /// Volume multiplier relative to rolling average.
    pub volume_multiplier: f64,
    /// Confidence score (0.0–1.0).
    pub confidence: f64,
    /// Computed stop-loss price (only set on buy/sell signals).
    pub stop_loss: Option<f64>,
    /// Computed take-profit price (only set on buy/sell signals).
    pub take_profit: Option<f64>,
    /// Human-readable reason string.
    pub reason: String,
}

// ============================================================================
// VWAP Calculator (internal)
// ============================================================================

/// Calculates the Volume-Weighted Average Price and its standard deviation.
///
/// Resets at the start of each session. For 24/7 crypto markets, call
/// `reset()` at whatever session boundary you define (e.g. 00:00 UTC).
#[derive(Debug)]
struct VwapCalculator {
    cumulative_tp_volume: f64,
    cumulative_volume: f64,
    squared_deviation_sum: f64,
    candle_count: usize,
}

impl VwapCalculator {
    fn new() -> Self {
        Self {
            cumulative_tp_volume: 0.0,
            cumulative_volume: 0.0,
            squared_deviation_sum: 0.0,
            candle_count: 0,
        }
    }

    /// Update with a new candle. Returns `(vwap, std_dev)`.
    ///
    /// If volume is zero or not available, we use a fallback volume of 1.0
    /// so VWAP still tracks the typical price (equal-weighted).
    fn update(&mut self, high: f64, low: f64, close: f64, volume: f64) -> (f64, f64) {
        let tp = (high + low + close) / 3.0;
        let vol = if volume > 0.0 { volume } else { 1.0 };

        self.cumulative_tp_volume += tp * vol;
        self.cumulative_volume += vol;
        self.candle_count += 1;

        let vwap = if self.cumulative_volume > 0.0 {
            self.cumulative_tp_volume / self.cumulative_volume
        } else {
            tp
        };

        let deviation = tp - vwap;
        self.squared_deviation_sum += deviation * deviation * vol;

        let std_dev = if self.cumulative_volume > 0.0 && self.candle_count > 1 {
            (self.squared_deviation_sum / self.cumulative_volume).sqrt()
        } else {
            0.0
        };

        (vwap, std_dev)
    }

    /// Reset for a new session.
    fn reset(&mut self) {
        self.cumulative_tp_volume = 0.0;
        self.cumulative_volume = 0.0;
        self.squared_deviation_sum = 0.0;
        self.candle_count = 0;
    }

    /// Number of candles processed since last reset.
    #[allow(dead_code)]
    fn candle_count(&self) -> usize {
        self.candle_count
    }
}

// ============================================================================
// Volume Analyzer (internal)
// ============================================================================

/// Rolling volume analyzer — detects volume spikes relative to the moving
/// average of recent candle volumes.
#[derive(Debug)]
struct VolumeAnalyzer {
    period: usize,
    volumes: Vec<f64>,
}

impl VolumeAnalyzer {
    fn new(period: usize) -> Self {
        Self {
            period: period.max(1),
            volumes: Vec::with_capacity(period),
        }
    }

    /// Push a new volume value. Returns the multiplier (volume / rolling avg).
    /// Returns 1.0 if there's no history yet.
    fn update(&mut self, volume: f64) -> f64 {
        let avg = self.average();

        self.volumes.push(volume);
        if self.volumes.len() > self.period {
            self.volumes.remove(0);
        }

        if avg > 0.0 { volume / avg } else { 1.0 }
    }

    /// Rolling average volume over the configured period.
    fn average(&self) -> f64 {
        if self.volumes.is_empty() {
            0.0
        } else {
            self.volumes.iter().sum::<f64>() / self.volumes.len() as f64
        }
    }
}

