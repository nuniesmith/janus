    // ── Stress test ─────────────────────────────────────────────────────

    #[test]
    fn test_many_candles_no_panic() {
        let mut strategy = VwapScalperStrategy::new(VwapScalperConfig::crypto_aggressive());
        let mut buy_count = 0u32;
        let mut sell_count = 0u32;

        for i in 0..1000 {
            let base = 50000.0 + (i as f64 * 0.1).sin() * 500.0;
            let vol = 1000.0 + (i as f64 * 0.05).cos().abs() * 3000.0;
            let result = strategy.update_ohlcv(
                base - 5.0,
                base + 20.0,
                base - 20.0,
                base + 5.0 * (i as f64 * 0.2).sin(),
                vol,
            );
            match result.signal {
                VwapSignal::Buy => buy_count += 1,
                VwapSignal::Sell => sell_count += 1,
                VwapSignal::Hold => {}
            }
        }

        assert_eq!(strategy.candle_count(), 1000);
        println!(
            "Stress test: {} buys, {} sells out of 1000 candles",
            buy_count, sell_count
        );
    }

