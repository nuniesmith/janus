/// Configuration for the VWAP Scalper strategy.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct VwapScalperConfig {
    /// VWAP standard-deviation multiplier for band width (default: 2.0).
    /// Higher values mean wider bands → fewer but higher-quality signals.
    pub vwap_std_dev: f64,

    /// ATR period for stop-loss / volatility measurement (default: 14).
    pub atr_period: usize,

    /// ATR multiplier for stop-loss distance (default: 1.5).
    pub atr_stop_multiplier: f64,

    /// Rolling volume lookback period for spike detection (default: 20).
    pub volume_period: usize,

    /// Minimum volume multiplier (vs rolling average) to boost confidence
    /// (default: 1.5). A candle with volume ≥ this × average gets +0.10
    /// confidence.
    pub min_volume_multiplier: f64,

    /// Minimum confidence score to emit a signal (default: 0.55).
    pub min_confidence: f64,

    /// Minimum candle body ratio (body / range) to count as "strong" candle
    /// (default: 0.5).
    pub min_body_ratio: f64,

    /// Minimum number of candles before the strategy is considered ready
    /// (default: 20). Ensures VWAP has enough data.
    pub warmup_candles: usize,
}

impl Default for VwapScalperConfig {
    fn default() -> Self {
        Self {
            vwap_std_dev: 2.0,
            atr_period: 14,
            atr_stop_multiplier: 1.5,
            volume_period: 20,
            min_volume_multiplier: 1.5,
            min_confidence: 0.55,
            min_body_ratio: 0.5,
            warmup_candles: 20,
        }
    }
}

impl VwapScalperConfig {
    /// Aggressive config for crypto — tighter bands, lower confidence threshold.
    pub fn crypto_aggressive() -> Self {
        Self {
            vwap_std_dev: 1.5,
            atr_stop_multiplier: 1.2,
            min_confidence: 0.50,
            min_body_ratio: 0.45,
            warmup_candles: 15,
            ..Default::default()
        }
    }

    /// Conservative config — wider bands, higher confidence required.
    pub fn conservative() -> Self {
        Self {
            vwap_std_dev: 2.5,
            atr_stop_multiplier: 2.0,
            min_confidence: 0.65,
            min_body_ratio: 0.55,
            warmup_candles: 30,
            ..Default::default()
        }
    }
}

