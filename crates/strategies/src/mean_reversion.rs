    // Exit conditions --------------------------------------------------------

    #[test]
    fn test_exit_at_middle_band() {
        // Use a config that exits at middle band
        let config = MeanReversionConfig {
            exit_at_middle: true,
            ..Default::default()
        };
        let mut strategy = MeanReversionStrategy::new(config);

        // Warm up
        for _ in 0..25 {
            strategy.update_hlc(101.0, 99.0, 100.0);
        }

        // Drive price down to trigger Buy
        for _ in 0..8 {
            strategy.update_hlc(96.0, 93.0, 94.0);
        }

        // If we got into a position, drive price back up toward middle
        if strategy.is_in_position() {
            assert!(strategy.entry_price().is_some());
            assert!(strategy.stop_loss().is_some());
            assert!(strategy.take_profit().is_some());

            // Price recovery — should eventually hit middle band take profit
            for _ in 0..10 {
                let signal = strategy.update_hlc(100.0, 98.0, 99.0);
                if signal == MeanReversionSignal::Sell {
                    assert!(
                        !strategy.is_in_position(),
                        "Position should be closed after TP"
                    );
                    return;
                }
            }
        }
        // If we never entered, that's fine — the test verifies no panics
    }

    #[test]
    fn test_stop_loss_exits_position() {
        let config = MeanReversionConfig {
            atr_stop_multiplier: 0.5, // Tight stop for testing
            min_band_width_pct: 0.0,  // Allow any width
            ..Default::default()
        };
        let mut strategy = MeanReversionStrategy::new(config);

        // Warm up with some volatility
        for i in 0..25 {
            let offset = (i as f64 * 0.3).sin() * 2.0;
            strategy.update_hlc(101.0 + offset, 99.0 + offset, 100.0 + offset);
        }

        // Try to trigger a buy by driving price well below lower band
        for _ in 0..10 {
            strategy.update_hlc(95.0, 92.0, 93.0);
        }

        if strategy.is_in_position() {
            let stop = strategy.stop_loss().unwrap();

            // Price crashes through stop loss
            let crash_price = stop - 5.0;
            let signal = strategy.update_hlc(crash_price + 0.5, crash_price - 0.5, crash_price);

            assert_eq!(
                signal,
                MeanReversionSignal::Sell,
                "Should sell on stop loss"
            );
            assert!(!strategy.is_in_position(), "Should exit position");
        }
    }

