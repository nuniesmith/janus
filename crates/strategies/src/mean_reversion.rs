    // Band-width squeeze filter ----------------------------------------------

    #[test]
    fn test_squeeze_filter() {
        let config = MeanReversionConfig {
            min_band_width_pct: 5.0, // Very high threshold
            ..Default::default()
        };
        let mut strategy = MeanReversionStrategy::new(config);

        // Feed very tight range data
        for i in 0..30 {
            let price = 100.0 + (i as f64 % 2.0) * 0.1;
            strategy.update_hlc(price + 0.05, price - 0.05, price);
        }

        // Should not generate signals due to narrow bands
        let signal = strategy.update_hlc(99.5, 99.3, 99.4);
        assert_eq!(signal, MeanReversionSignal::Hold);
    }

    #[test]
    fn test_squeeze_reason() {
        let config = MeanReversionConfig {
            min_band_width_pct: 50.0, // Absurdly high — guarantees squeeze
            ..Default::default()
        };
        let mut strategy = MeanReversionStrategy::new(config);

        warm_up(&mut strategy, 25, 100.0);

        let result = strategy.update_hlc_with_reason(100.5, 99.5, 100.0);
        if result.signal == MeanReversionSignal::Hold && result.reason.contains("squeeze") {
            // Good — squeeze was detected and reported
        }
        // Either way, no crash
    }

