    #[test]
    fn test_rsi_confirmation_blocks_entry() {
        let config = MeanReversionConfig {
            require_rsi_confirmation: true,
            rsi_oversold: 20.0, // Very strict — RSI must be < 20
            ..Default::default()
        };
        let mut strategy = MeanReversionStrategy::new(config);

        // Warm up
        for _ in 0..25 {
            strategy.update_hlc(101.0, 99.0, 100.0);
        }

        // Even if %B is low, RSI may not be < 20, so entry should be blocked
        for _ in 0..5 {
            let signal = strategy.update_hlc(96.0, 93.0, 94.0);
            // With strict RSI, we expect Hold (RSI probably won't drop to 20
            // with just 5 bars of decline from a stable series)
            if signal == MeanReversionSignal::Buy {
                // If RSI actually confirms, that's also valid
                assert!(
                    strategy.last_rsi().unwrap_or(100.0) <= 20.0,
                    "RSI should be oversold for buy: {:?}",
                    strategy.last_rsi()
                );
            }
        }
    }

