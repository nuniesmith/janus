    /// Warm up the strategy with `n` bars of gentle upward-trending data so
    /// Bollinger Bands, RSI, and ATR all become ready.
    fn warm_up(strategy: &mut MeanReversionStrategy, n: usize, base_price: f64) {
        for i in 0..n {
            let price = base_price + (i as f64) * 0.1;
            strategy.update_hlc(price + 1.0, price - 1.0, price);
        }
    }

