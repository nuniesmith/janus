//! Mean Reversion Strategy using Bollinger Bands
//!
//! Ported from the Kraken trading bot's mean reversion strategy, adapted to use
//! `janus-regime` indicators and `janus-models::Candle` types.
//!
//! Works best in ranging/mean-reverting market regimes. Buys when price touches
//! the lower Bollinger Band, sells when it touches the upper band. Includes
//! RSI confirmation, ATR-based stop losses, and band-width squeeze filtering.
//!
//! ## Regime-Aware Usage
//!
//! This strategy is designed to be gated by the regime detection system. Use
//! [`MeanReversionStrategy::should_trade_in_regime`] to check whether the
//! current market regime is suitable before acting on signals.
//!
//! ```rust,no_run
//! use janus_strategies::mean_reversion::{MeanReversionStrategy, MeanReversionConfig};
//! use janus_regime::{MarketRegime, EnsembleRegimeDetector};
//!
//! let mut strategy = MeanReversionStrategy::new(MeanReversionConfig::default());
//! let mut detector = EnsembleRegimeDetector::default_config();
//!
//! // On each candle:
//! // let regime_result = detector.update(high, low, close);
//! // if strategy.should_trade_in_regime(&regime_result.regime) {
//! //     let signal = strategy.update_hlc(high, low, close);
//! //     // act on signal...
//! // }
//! ```

use janus_regime::MarketRegime;
use janus_regime::indicators::{ATR, BollingerBands, BollingerBandsValues, RSI};
use serde::{Deserialize, Serialize};

/// Signal generated by the mean reversion strategy
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum MeanReversionSignal {
    /// Enter a long position (price at lower band)
    Buy,
    /// Exit long / enter short (price at upper band)
    Sell,
    /// No action
    Hold,
}

impl std::fmt::Display for MeanReversionSignal {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            MeanReversionSignal::Buy => write!(f, "BUY"),
            MeanReversionSignal::Sell => write!(f, "SELL"),
            MeanReversionSignal::Hold => write!(f, "HOLD"),
        }
    }
}

/// Configuration for the mean reversion strategy
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct MeanReversionConfig {
    /// Bollinger Bands period (default: 20)
    pub bb_period: usize,
    /// Bollinger Bands standard deviation multiplier (default: 2.0)
    pub bb_std_dev: f64,

    /// Entry threshold — how close to band before considering entry.
    /// 0.05 = price within 5% of band width from the band.
    pub entry_threshold: f64,

    /// If true, exit at middle band (SMA); if false, exit at opposite band.
    pub exit_at_middle: bool,

    /// RSI period for confirmation (default: 14)
    pub rsi_period: usize,
    /// RSI oversold threshold for buy confirmation (default: 30.0)
    pub rsi_oversold: f64,
    /// RSI overbought threshold for sell confirmation (default: 70.0)
    pub rsi_overbought: f64,

    /// Whether RSI confirmation is required before entry
    pub require_rsi_confirmation: bool,

    /// ATR period for stop loss calculation (default: 14)
    pub atr_period: usize,

    /// ATR multiplier for stop loss distance (default: 2.0)
    pub atr_stop_multiplier: f64,

    /// Minimum band width (as % of price) to trade.
    /// Avoids trading in Bollinger Band squeeze conditions.
    pub min_band_width_pct: f64,
}

impl Default for MeanReversionConfig {
    fn default() -> Self {
        Self {
            bb_period: 20,
            bb_std_dev: 2.0,
            entry_threshold: 0.05,
            exit_at_middle: true,
            rsi_period: 14,
            rsi_oversold: 30.0,
            rsi_overbought: 70.0,
            require_rsi_confirmation: false,
            atr_period: 14,
            atr_stop_multiplier: 2.0,
            min_band_width_pct: 2.0,
        }
    }
}

