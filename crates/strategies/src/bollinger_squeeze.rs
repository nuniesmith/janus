    /// Feed a steady stream of similar-priced candles to warm up indicators.
    fn warm_up(strategy: &mut SqueezeBreakoutStrategy, center: f64, count: usize) {
        for i in 0..count {
            let phase = (i as f64) * std::f64::consts::PI / 7.0;
            let high = center + 50.0 + 20.0 * phase.sin().abs();
            let low = center - 50.0 - 20.0 * phase.cos().abs();
            let open = center + 10.0 * phase.cos();
            let close = center + 30.0 * phase.sin();
            strategy.update_ohlc(open, high, low, close);
        }
    }

    /// Feed a tight-range squeeze pattern.
    #[allow(dead_code)]
    fn feed_squeeze(strategy: &mut SqueezeBreakoutStrategy, center: f64, count: usize) {
        for i in 0..count {
            let tiny = (i as f64) * 0.01;
            let open = center - 0.5 + tiny;
            let high = center + 1.0 + tiny;
            let low = center - 1.0 + tiny;
            let close = center + 0.5 + tiny;
            strategy.update_ohlc(open, high, low, close);
        }
    }

