    #[test]
    fn test_squeeze_detection_on_tight_range() {
        let mut strategy = SqueezeBreakoutStrategy::new(SqueezeBreakoutConfig {
            bb_period: 20,
            bb_std_dev: 2.0,
            squeeze_threshold: 5.0, // Generous threshold for testing (5% of price)
            min_squeeze_candles: 3,
            ..Default::default()
        });

        // Warm up with normal volatility
        warm_up(&mut strategy, 1000.0, 25);

        // Now feed very tight-range candles
        for i in 0..20 {
            let tiny = (i as f64) * 0.001;
            let result = strategy.update_ohlc(
                1000.0 - 0.1 + tiny,
                1000.0 + 0.5 + tiny,
                1000.0 - 0.5 + tiny,
                1000.0 + 0.1 + tiny,
            );
            // After enough tight candles, should detect squeeze
            if strategy.is_in_squeeze() {
                assert!(
                    result.signal == SqueezeBreakoutSignal::Squeeze
                        || result.signal == SqueezeBreakoutSignal::Hold,
                    "In squeeze should report Squeeze or Hold, got: {:?}",
                    result.signal
                );
            }
        }
    }

