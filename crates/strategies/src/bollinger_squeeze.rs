/// Configuration for the Bollinger Squeeze Breakout strategy.
#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct SqueezeBreakoutConfig {
    // Bollinger Band parameters
    /// Bollinger Band lookback period (default: 20)
    pub bb_period: usize,
    /// Bollinger Band standard deviation multiplier (default: 2.0)
    pub bb_std_dev: f64,

    // Squeeze detection
    /// Squeeze threshold — Bollinger Band width (as percentage of price,
    /// where 3% = 3.0) below which the market is considered "in a squeeze"
    /// (default: 3.0). The BB width from `janus-regime` is already scaled
    /// as `(upper - lower) / middle * 100`.
    pub squeeze_threshold: f64,
    /// Minimum number of candles in squeeze before a breakout is valid
    /// (default: 5). Longer squeezes tend to produce more reliable breakouts.
    pub min_squeeze_candles: usize,

    // Trend confirmation
    /// Short EMA period for trend direction (default: 50)
    pub ema_short_period: usize,
    /// Long EMA period for trend direction (default: 200)
    pub ema_long_period: usize,
    /// ADX period for trend strength measurement (default: 14)
    pub adx_period: usize,
    /// ADX threshold — values above this indicate a trending market (default: 25.0)
    pub adx_threshold: f64,

    // Risk management
    /// ATR multiplier for stop loss distance (default: 2.0)
    pub atr_stop_multiplier: f64,
    /// ATR multiplier for take profit distance (default: 4.0)
    pub atr_tp_multiplier: f64,

    // Signal quality
    /// Minimum confidence score to emit a signal (default: 0.6)
    pub min_confidence: f64,
    /// Minimum candle body ratio (body_size / range) for a "strong" candle
    /// (default: 0.6). Breakouts on strong candles get extra confidence.
    pub min_body_ratio: f64,
}

impl Default for SqueezeBreakoutConfig {
    fn default() -> Self {
        Self {
            bb_period: 20,
            bb_std_dev: 2.0,
            squeeze_threshold: 3.0,
            min_squeeze_candles: 5,
            ema_short_period: 50,
            ema_long_period: 200,
            adx_period: 14,
            adx_threshold: 25.0,
            atr_stop_multiplier: 2.0,
            atr_tp_multiplier: 4.0,
            min_confidence: 0.6,
            min_body_ratio: 0.6,
        }
    }
}

impl SqueezeBreakoutConfig {
    /// Aggressive config for crypto — tighter squeeze, lower confidence threshold.
    pub fn crypto_aggressive() -> Self {
        Self {
            squeeze_threshold: 2.5,
            min_squeeze_candles: 4,
            atr_stop_multiplier: 1.5,
            atr_tp_multiplier: 3.5,
            min_confidence: 0.55,
            min_body_ratio: 0.55,
            ..Default::default()
        }
    }

    /// Conservative config — requires longer squeeze, higher confidence.
    pub fn conservative() -> Self {
        Self {
            squeeze_threshold: 3.5,
            min_squeeze_candles: 8,
            atr_stop_multiplier: 2.5,
            atr_tp_multiplier: 5.0,
            min_confidence: 0.70,
            min_body_ratio: 0.65,
            ..Default::default()
        }
    }
}

