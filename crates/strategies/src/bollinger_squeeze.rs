//! # Bollinger Squeeze Breakout Strategy
//!
//! Identifies periods of low volatility (Bollinger Band squeeze) and generates
//! breakout signals when price closes outside the bands after a squeeze period.
//!
//! ## How It Works
//!
//! 1. **Squeeze Detection**: Bollinger Band width falls below a configurable
//!    threshold (as a percentage of price). This indicates consolidation.
//!
//! 2. **Breakout Signal**: When price breaks above the upper band (bullish) or
//!    below the lower band (bearish) after being in a squeeze for at least
//!    `min_squeeze_candles`, a breakout signal is generated.
//!
//! 3. **Trend Confirmation**: EMA short/long crossover confirms the breakout
//!    direction. ADX strength adds confidence.
//!
//! 4. **ATR-Based Stops**: Stop loss is placed at the Bollinger middle band
//!    minus an ATR multiple. Take profit uses a larger ATR multiple.
//!
//! ## Regime Integration
//!
//! This strategy is most effective during transitions from ranging to trending
//! regimes. Use `should_trade_in_regime()` to gate activation:
//!
//! - **MeanReverting** → Yes (squeeze forms during ranging markets)
//! - **Uncertain** → Yes with reduced size (squeeze may be forming)
//! - **Trending** → No (breakout already happened)
//! - **Volatile** → No (not a true squeeze breakout)
//!
//! ## Origin
//!
//! Ported from Kraken's `strategy/swing.rs::BollingerSqueeze`, adapted for
//! the JANUS single-asset design, `janus-regime` indicator types, and
//! regime-aware position sizing.

use janus_regime::MarketRegime;
use janus_regime::indicators::{ADX, ATR, BollingerBands, BollingerBandsValues, EMA};
use serde::{Deserialize, Serialize};

// ============================================================================
// Signal Types
// ============================================================================

/// Signal generated by the Bollinger Squeeze Breakout strategy.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum SqueezeBreakoutSignal {
    /// Bullish breakout — price closed above upper Bollinger Band after squeeze.
    BuyBreakout,
    /// Bearish breakout — price closed below lower Bollinger Band after squeeze.
    SellBreakout,
    /// Currently in a squeeze (consolidation) — no action yet.
    Squeeze,
    /// No signal — not in squeeze and no breakout detected.
    Hold,
}

impl std::fmt::Display for SqueezeBreakoutSignal {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            SqueezeBreakoutSignal::BuyBreakout => write!(f, "BuyBreakout"),
            SqueezeBreakoutSignal::SellBreakout => write!(f, "SellBreakout"),
            SqueezeBreakoutSignal::Squeeze => write!(f, "Squeeze"),
            SqueezeBreakoutSignal::Hold => write!(f, "Hold"),
        }
    }
}

