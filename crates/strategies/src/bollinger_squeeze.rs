/// Detailed result from a Bollinger Squeeze Breakout strategy update.
#[derive(Debug, Clone)]
pub struct SqueezeBreakoutResult {
    /// The signal produced.
    pub signal: SqueezeBreakoutSignal,
    /// Bollinger Band values at the time of the signal (if available).
    pub bb_values: Option<BollingerBandsValues>,
    /// Current ATR value (if available).
    pub atr: Option<f64>,
    /// Current ADX value (if available).
    pub adx: Option<f64>,
    /// Whether the strategy is currently in a squeeze.
    pub in_squeeze: bool,
    /// How many candles the current (or most recent) squeeze lasted.
    pub squeeze_duration: usize,
    /// Confidence score for the signal (0.0–1.0).
    pub confidence: f64,
    /// Computed stop loss price (only set on breakout signals).
    pub stop_loss: Option<f64>,
    /// Computed take profit price (only set on breakout signals).
    pub take_profit: Option<f64>,
    /// Human-readable reason string.
    pub reason: String,
}

