/// Bollinger Squeeze Breakout Strategy
///
/// Detects low-volatility squeeze periods via Bollinger Band width and
/// generates breakout signals when price escapes the bands after a squeeze.
/// Uses EMA trend direction and ADX strength for confirmation.
///
/// This is a single-asset strategy — instantiate one per symbol.
#[derive(Debug)]
pub struct SqueezeBreakoutStrategy {
    config: SqueezeBreakoutConfig,

    // Indicators
    bb: BollingerBands,
    ema_short: EMA,
    ema_long: EMA,
    adx: ADX,
    atr: ATR,

    // Squeeze tracking
    in_squeeze: bool,
    squeeze_candles: usize,
    /// True if we were in a squeeze on the *previous* candle (for transition detection).
    was_in_squeeze: bool,
    /// Duration of the squeeze that just ended (if any).
    last_squeeze_duration: usize,

    // Position tracking
    in_position: bool,
    entry_price: Option<f64>,
    entry_side: Option<SqueezeBreakoutSignal>,
    stop_loss: Option<f64>,
    take_profit: Option<f64>,

    // Last computed values
    last_bb: Option<BollingerBandsValues>,
    last_adx: Option<f64>,
    last_atr: Option<f64>,
    last_signal: SqueezeBreakoutSignal,
    candle_count: usize,
}

impl SqueezeBreakoutStrategy {
    /// Create a new Bollinger Squeeze Breakout strategy with the given config.
    pub fn new(config: SqueezeBreakoutConfig) -> Self {
        Self {
            bb: BollingerBands::new(config.bb_period, config.bb_std_dev),
            ema_short: EMA::new(config.ema_short_period),
            ema_long: EMA::new(config.ema_long_period),
            adx: ADX::new(config.adx_period),
            atr: ATR::new(config.adx_period), // ATR uses same period as ADX by convention
            config,
            in_squeeze: false,
            squeeze_candles: 0,
            was_in_squeeze: false,
            last_squeeze_duration: 0,
            in_position: false,
            entry_price: None,
            entry_side: None,
            stop_loss: None,
            take_profit: None,
            last_bb: None,
            last_adx: None,
            last_atr: None,
            last_signal: SqueezeBreakoutSignal::Hold,
            candle_count: 0,
        }
    }

    /// Create with default configuration.
    pub fn with_defaults() -> Self {
        Self::new(SqueezeBreakoutConfig::default())
    }

    /// Update the strategy with a `janus-models` Candle.
    pub fn update_candle(&mut self, candle: &janus_models::Candle) -> SqueezeBreakoutSignal {
        self.update_ohlc(candle.open, candle.high, candle.low, candle.close)
            .signal
    }

    /// Update the strategy with a `janus-models` Candle and return detailed result.
    pub fn update_candle_with_reason(
        &mut self,
        candle: &janus_models::Candle,
    ) -> SqueezeBreakoutResult {
        self.update_ohlc(candle.open, candle.high, candle.low, candle.close)
    }

    /// Update with raw OHLC values and return a detailed result.
    pub fn update_ohlc(
        &mut self,
        open: f64,
        high: f64,
        low: f64,
        close: f64,
    ) -> SqueezeBreakoutResult {
        self.candle_count += 1;

        // Update all indicators
        self.ema_short.update(close);
        self.ema_long.update(close);
        self.adx.update(high, low, close);
        self.atr.update(high, low, close);

        // BollingerBands::update takes a single price (close)
        let bb_values = self.bb.update(close);
        self.last_bb = bb_values;
        self.last_adx = self.adx.value();
        self.last_atr = self.atr.value();

        // Save pre-update squeeze state for transition detection
        self.was_in_squeeze = self.in_squeeze;
        let prev_squeeze_duration = self.squeeze_candles;

        // Update squeeze detection
        if let Some(ref bbv) = bb_values {
            let previously_in_squeeze = self.in_squeeze;
            self.in_squeeze = bbv.width < self.config.squeeze_threshold;

            if self.in_squeeze {
                self.squeeze_candles += 1;
            } else if previously_in_squeeze {
                // Just exited squeeze — record the duration
                self.last_squeeze_duration = self.squeeze_candles;
                self.squeeze_candles = 0;
            }
        }

        // Not enough data yet
        if !self.bb.is_ready() || !self.atr.is_ready() {
            let signal = if self.in_squeeze {
                SqueezeBreakoutSignal::Squeeze
            } else {
                SqueezeBreakoutSignal::Hold
            };
            self.last_signal = signal;
            return SqueezeBreakoutResult {
                signal,
                bb_values,
                atr: self.last_atr,
                adx: self.last_adx,
                in_squeeze: self.in_squeeze,
                squeeze_duration: self.squeeze_candles,
                confidence: 0.0,
                stop_loss: None,
                take_profit: None,
                reason: "Hold: Indicators warming up".to_string(),
            };
        }

        // Check for exits if in position
        if self.in_position {
            let signal = self.check_exit(close);
            if signal != SqueezeBreakoutSignal::Hold {
                return self.make_result(signal, close, open, high, low);
            }
            // Still in position, no exit triggered
            return self.make_result(SqueezeBreakoutSignal::Hold, close, open, high, low);
        }

        // Check for breakout entry
        // Conditions: was in squeeze for min_squeeze_candles, now breaking out
        if self.was_in_squeeze
            && prev_squeeze_duration >= self.config.min_squeeze_candles
            && !self.in_squeeze
            && let Some(ref bbv) = bb_values
        {
            let atr = self.last_atr.unwrap_or(close * 0.02);
            let body_size = (close - open).abs();
            let range = high - low;
            let body_ratio = if range > 0.0 { body_size / range } else { 0.0 };
            let is_bullish = close > open;
            let is_bearish = close < open;

            // EMA trend direction
            let ema_short_val = self.ema_short.value();
            let ema_long_val = self.ema_long.value();
            let ema_bullish = match (ema_short_val, ema_long_val) {
                (Some(s), Some(l)) => s > l,
                _ => false,
            };
            let ema_bearish = match (ema_short_val, ema_long_val) {
                (Some(s), Some(l)) => s < l,
                _ => false,
            };

            let adx_val = self.last_adx.unwrap_or(0.0);

            // === BULLISH BREAKOUT ===
            if close > bbv.upper && is_bullish {
                let mut confidence = 0.5;
                if ema_bullish {
                    confidence += 0.20;
                }
                if prev_squeeze_duration > 10 {
                    confidence += 0.10; // Longer squeeze = more reliable
                }
                if body_ratio > self.config.min_body_ratio {
                    confidence += 0.10; // Strong candle body
                }
                if adx_val > self.config.adx_threshold {
                    confidence += 0.05; // Trend strength confirmation
                }

                if confidence >= self.config.min_confidence {
                    let stop = bbv.middle - atr * self.config.atr_stop_multiplier;
                    let tp = close + atr * self.config.atr_tp_multiplier;

                    self.enter_position(SqueezeBreakoutSignal::BuyBreakout, close, stop, tp);

                    let signal = SqueezeBreakoutSignal::BuyBreakout;
                    self.last_signal = signal;
                    return SqueezeBreakoutResult {
                        signal,
                        bb_values: self.last_bb,
                        atr: self.last_atr,
                        adx: self.last_adx,
                        in_squeeze: false,
                        squeeze_duration: prev_squeeze_duration,
                        confidence,
                        stop_loss: Some(stop),
                        take_profit: Some(tp),
                        reason: format!(
                            "BuyBreakout: price {:.2} > upper BB {:.2}, squeeze {} candles, conf {:.0}%",
                            close,
                            bbv.upper,
                            prev_squeeze_duration,
                            confidence * 100.0
                        ),
                    };
                }
            }

            // === BEARISH BREAKOUT ===
            if close < bbv.lower && is_bearish {
                let mut confidence = 0.5;
                if ema_bearish {
                    confidence += 0.20;
                }
                if prev_squeeze_duration > 10 {
                    confidence += 0.10;
                }
                if body_ratio > self.config.min_body_ratio {
                    confidence += 0.10;
                }
                if adx_val > self.config.adx_threshold {
                    confidence += 0.05;
                }

                if confidence >= self.config.min_confidence {
                    let stop = bbv.middle + atr * self.config.atr_stop_multiplier;
                    let tp = close - atr * self.config.atr_tp_multiplier;

                    self.enter_position(SqueezeBreakoutSignal::SellBreakout, close, stop, tp);

                    let signal = SqueezeBreakoutSignal::SellBreakout;
                    self.last_signal = signal;
                    return SqueezeBreakoutResult {
                        signal,
                        bb_values: self.last_bb,
                        atr: self.last_atr,
                        adx: self.last_adx,
                        in_squeeze: false,
                        squeeze_duration: prev_squeeze_duration,
                        confidence,
                        stop_loss: Some(stop),
                        take_profit: Some(tp),
                        reason: format!(
                            "SellBreakout: price {:.2} < lower BB {:.2}, squeeze {} candles, conf {:.0}%",
                            close,
                            bbv.lower,
                            prev_squeeze_duration,
                            confidence * 100.0
                        ),
                    };
                }
            }
        }

        // Currently in squeeze — report it
        if self.in_squeeze {
            let signal = SqueezeBreakoutSignal::Squeeze;
            self.last_signal = signal;
            return SqueezeBreakoutResult {
                signal,
                bb_values,
                atr: self.last_atr,
                adx: self.last_adx,
                in_squeeze: true,
                squeeze_duration: self.squeeze_candles,
                confidence: 0.0,
                stop_loss: None,
                take_profit: None,
                reason: format!(
                    "Squeeze: BB width {:.4} < threshold {:.4}, {} candles",
                    bb_values.as_ref().map(|b| b.width).unwrap_or(0.0),
                    self.config.squeeze_threshold,
                    self.squeeze_candles,
                ),
            };
        }

        // No signal
        let signal = SqueezeBreakoutSignal::Hold;
        self.last_signal = signal;
        self.make_result(signal, close, open, high, low)
    }

    /// Update with HLC values only (open defaults to close, matching tick-aggregated
    /// candles from RegimeManager which don't have distinct open).
    pub fn update_hlc(&mut self, high: f64, low: f64, close: f64) -> SqueezeBreakoutResult {
        // Use close as open approximation for tick-aggregated candles
        self.update_ohlc(close, high, low, close)
    }

    // ========================================================================
    // Regime gating
    // ========================================================================

    /// Check whether this strategy should trade in the given market regime.
    ///
    /// Squeeze breakouts form during ranging/mean-reverting markets and fire
    /// when the market transitions. The strategy should NOT be active during
    /// already-trending or highly volatile regimes.
    pub fn should_trade_in_regime(regime: &MarketRegime) -> bool {
        matches!(
            regime,
            MarketRegime::MeanReverting | MarketRegime::Uncertain
        )
    }

    /// Compute a confidence-weighted position size factor for the given regime.
    ///
    /// Returns `Some(factor)` if the regime is suitable, `None` otherwise.
    /// Factor ranges from 0.0 to 1.0.
    pub fn regime_size_factor(regime: &MarketRegime, confidence: f64) -> Option<f64> {
        match regime {
            MarketRegime::MeanReverting => {
                // Squeeze breakouts from ranging → trending are ideal
                Some(confidence.clamp(0.4, 1.0))
            }
            MarketRegime::Uncertain => {
                // May be transitioning — allow with reduced size
                if confidence < 0.5 {
                    Some(0.30)
                } else {
                    Some(0.20)
                }
            }
            _ => None,
        }
    }

    // ========================================================================
    // Position management
    // ========================================================================

    fn enter_position(&mut self, side: SqueezeBreakoutSignal, price: f64, stop: f64, tp: f64) {
        self.in_position = true;
        self.entry_price = Some(price);
        self.entry_side = Some(side);
        self.stop_loss = Some(stop);
        self.take_profit = Some(tp);
    }

    fn check_exit(&mut self, close: f64) -> SqueezeBreakoutSignal {
        let stop = self.stop_loss.unwrap_or(0.0);
        let tp = self.take_profit.unwrap_or(f64::MAX);

        match self.entry_side {
            Some(SqueezeBreakoutSignal::BuyBreakout) => {
                // Stop loss hit
                if close <= stop {
                    self.exit_position();
                    return SqueezeBreakoutSignal::SellBreakout; // Exit signal
                }
                // Take profit hit
                if close >= tp {
                    self.exit_position();
                    return SqueezeBreakoutSignal::SellBreakout;
                }
            }
            Some(SqueezeBreakoutSignal::SellBreakout) => {
                if close >= stop {
                    self.exit_position();
                    return SqueezeBreakoutSignal::BuyBreakout;
                }
                if close <= tp {
                    self.exit_position();
                    return SqueezeBreakoutSignal::BuyBreakout;
                }
            }
            _ => {}
        }

        SqueezeBreakoutSignal::Hold
    }

    fn exit_position(&mut self) {
        self.in_position = false;
        self.entry_price = None;
        self.entry_side = None;
        self.stop_loss = None;
        self.take_profit = None;
    }

    fn make_result(
        &self,
        signal: SqueezeBreakoutSignal,
        _close: f64,
        _open: f64,
        _high: f64,
        _low: f64,
    ) -> SqueezeBreakoutResult {
        SqueezeBreakoutResult {
            signal,
            bb_values: self.last_bb,
            atr: self.last_atr,
            adx: self.last_adx,
            in_squeeze: self.in_squeeze,
            squeeze_duration: self.squeeze_candles,
            confidence: 0.0,
            stop_loss: self.stop_loss,
            take_profit: self.take_profit,
            reason: match signal {
                SqueezeBreakoutSignal::Hold => "Hold: No signal".to_string(),
                SqueezeBreakoutSignal::Squeeze => {
                    format!("Squeeze: {} candles", self.squeeze_candles,)
                }
                _ => format!("{}", signal),
            },
        }
    }

    // ========================================================================
    // Accessors
    // ========================================================================

    /// Reset all strategy state.
    pub fn reset(&mut self) {
        self.bb.reset();
        self.ema_short.reset();
        self.ema_long.reset();
        self.adx.reset();
        self.atr.reset();
        self.in_squeeze = false;
        self.squeeze_candles = 0;
        self.was_in_squeeze = false;
        self.last_squeeze_duration = 0;
        self.exit_position();
        self.last_bb = None;
        self.last_adx = None;
        self.last_atr = None;
        self.last_signal = SqueezeBreakoutSignal::Hold;
        self.candle_count = 0;
    }

    /// Whether the strategy has enough data to produce meaningful signals.
    pub fn is_ready(&self) -> bool {
        self.bb.is_ready() && self.atr.is_ready()
    }

    /// Whether the strategy is currently tracking an open position.
    pub fn is_in_position(&self) -> bool {
        self.in_position
    }

    /// Whether the market is currently in a squeeze.
    pub fn is_in_squeeze(&self) -> bool {
        self.in_squeeze
    }

    /// Number of consecutive candles in the current squeeze.
    pub fn squeeze_duration(&self) -> usize {
        self.squeeze_candles
    }

    /// Duration of the most recently completed squeeze.
    pub fn last_completed_squeeze_duration(&self) -> usize {
        self.last_squeeze_duration
    }

    /// Last computed Bollinger Band values.
    pub fn last_bb_values(&self) -> Option<BollingerBandsValues> {
        self.last_bb
    }

    /// Last computed ADX value.
    pub fn last_adx(&self) -> Option<f64> {
        self.last_adx
    }

    /// Last computed ATR value.
    pub fn last_atr(&self) -> Option<f64> {
        self.last_atr
    }

    /// Current entry price (if in position).
    pub fn entry_price(&self) -> Option<f64> {
        self.entry_price
    }

    /// Current stop loss (if in position).
    pub fn stop_loss(&self) -> Option<f64> {
        self.stop_loss
    }

    /// Current take profit (if in position).
    pub fn take_profit(&self) -> Option<f64> {
        self.take_profit
    }

    /// The last signal emitted.
    pub fn last_signal(&self) -> SqueezeBreakoutSignal {
        self.last_signal
    }

    /// Number of candles processed.
    pub fn candle_count(&self) -> usize {
        self.candle_count
    }

    /// Reference to the current configuration.
    pub fn config(&self) -> &SqueezeBreakoutConfig {
        &self.config
    }
}

