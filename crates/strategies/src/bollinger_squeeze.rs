    #[test]
    fn test_many_candles_no_panic() {
        let mut strategy = SqueezeBreakoutStrategy::with_defaults();

        // Feed 500 candles with varying patterns — no panics expected
        for i in 0..500 {
            let phase = (i as f64) * std::f64::consts::PI / 10.0;
            let base = 50000.0 + 500.0 * phase.sin();
            let volatility = 50.0 + 200.0 * ((i as f64) * 0.03).sin().abs();
            let open = base - volatility * 0.3;
            let high = base + volatility;
            let low = base - volatility;
            let close = base + volatility * 0.4 * phase.cos();

            let result = strategy.update_ohlc(open, high, low, close);
            assert!(
                matches!(
                    result.signal,
                    SqueezeBreakoutSignal::BuyBreakout
                        | SqueezeBreakoutSignal::SellBreakout
                        | SqueezeBreakoutSignal::Squeeze
                        | SqueezeBreakoutSignal::Hold
                ),
                "Signal should be a valid variant"
            );
        }

        assert_eq!(strategy.candle_count(), 500);
    }

