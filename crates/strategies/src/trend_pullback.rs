/// Trend Pullback Strategy
///
/// Identifies Fibonacci retracement entries within established trends,
/// confirmed by RSI and candlestick reversal patterns. Single-asset —
/// instantiate one per symbol.
#[derive(Debug)]
pub struct TrendPullbackStrategy {
    config: TrendPullbackConfig,

    // Indicators
    ema_short: EMA,
    ema_long: EMA,
    rsi: RSI,
    atr: ATR,

    // Candle history for swing points and patterns
    candles: Vec<CandleData>,

    // Position tracking
    in_position: bool,
    entry_price: Option<f64>,
    entry_side: Option<TrendPullbackSignal>,
    stop_loss: Option<f64>,
    take_profit: Option<f64>,

    // Last computed values (cached)
    last_ema_short: Option<f64>,
    last_ema_long: Option<f64>,
    last_rsi: Option<f64>,
    last_atr: Option<f64>,
    last_signal: TrendPullbackSignal,
    candle_count: usize,

    // HTF trend bias (optional)
    htf_trend: Option<TrendDirection>,
}

impl TrendPullbackStrategy {
    /// Create a new Trend Pullback strategy with the given config.
    pub fn new(config: TrendPullbackConfig) -> Self {
        let ema_short = EMA::new(config.ema_short_period);
        let ema_long = EMA::new(config.ema_long_period);
        let rsi = RSI::new(config.rsi_period);
        let atr = ATR::new(config.atr_period);

        Self {
            ema_short,
            ema_long,
            rsi,
            atr,
            candles: Vec::with_capacity(config.swing_lookback + 5),
            config,
            in_position: false,
            entry_price: None,
            entry_side: None,
            stop_loss: None,
            take_profit: None,
            last_ema_short: None,
            last_ema_long: None,
            last_rsi: None,
            last_atr: None,
            last_signal: TrendPullbackSignal::Hold,
            candle_count: 0,
            htf_trend: None,
        }
    }

    /// Create with default configuration.
    pub fn with_defaults() -> Self {
        Self::new(TrendPullbackConfig::default())
    }

    /// Set higher-timeframe trend bias.
    pub fn set_htf_trend(&mut self, trend: TrendDirection) {
        self.htf_trend = Some(trend);
    }

    /// Clear the HTF trend bias.
    pub fn clear_htf_trend(&mut self) {
        self.htf_trend = None;
    }

    /// Update with a `janus-models` Candle.
    pub fn update_candle(&mut self, candle: &janus_models::Candle) -> TrendPullbackSignal {
        self.update_ohlcv(
            candle.open,
            candle.high,
            candle.low,
            candle.close,
            candle.volume,
        )
        .signal
    }

    /// Update with a `janus-models` Candle and return detailed result.
    pub fn update_candle_with_reason(
        &mut self,
        candle: &janus_models::Candle,
    ) -> TrendPullbackResult {
        self.update_ohlcv(
            candle.open,
            candle.high,
            candle.low,
            candle.close,
            candle.volume,
        )
    }

    /// Main update with OHLCV values — returns a detailed result.
    pub fn update_ohlcv(
        &mut self,
        open: f64,
        high: f64,
        low: f64,
        close: f64,
        _volume: f64,
    ) -> TrendPullbackResult {
        self.candle_count += 1;

        // Update indicators
        self.ema_short.update(close);
        self.ema_long.update(close);
        let rsi_val = self.rsi.update(close);
        self.atr.update(high, low, close);

        self.last_ema_short = self.ema_short.value();
        self.last_ema_long = self.ema_long.value();
        self.last_rsi = rsi_val;
        self.last_atr = self.atr.value();

        // Store candle for pattern detection
        let candle_data = CandleData {
            open,
            high,
            low,
            close,
        };
        self.candles.push(candle_data);
        let max_candles = self.config.swing_lookback + 5;
        if self.candles.len() > max_candles {
            self.candles.remove(0);
        }

        // Not ready yet?
        if self.candle_count < self.config.warmup_candles {
            return self.make_result(
                TrendPullbackSignal::Hold,
                None,
                None,
                0.0,
                "Warming up".to_string(),
            );
        }

        let ema_s = match self.last_ema_short {
            Some(v) => v,
            None => {
                return self.make_result(
                    TrendPullbackSignal::Hold,
                    None,
                    None,
                    0.0,
                    "Short EMA not ready".to_string(),
                );
            }
        };
        let ema_l = match self.last_ema_long {
            Some(v) => v,
            None => {
                return self.make_result(
                    TrendPullbackSignal::Hold,
                    None,
                    None,
                    0.0,
                    "Long EMA not ready".to_string(),
                );
            }
        };

        let atr_val = match self.last_atr {
            Some(a) if a > 0.0 => a,
            _ => {
                return self.make_result(
                    TrendPullbackSignal::Hold,
                    None,
                    None,
                    0.0,
                    "ATR not ready".to_string(),
                );
            }
        };

        // RSI may not be ready yet — that's fine, we'll just skip the RSI bonus
        let rsi_now = self.last_rsi;

        // Determine trend from EMAs
        let trend = if close > ema_l && ema_s > ema_l {
            Some(TrendDirection::Bullish)
        } else if close < ema_l && ema_s < ema_l {
            Some(TrendDirection::Bearish)
        } else {
            None
        };

        // HTF agreement
        let htf = self.htf_trend;

        // Compute swing high/low from candle history
        let (swing_high, swing_low) = self.compute_swing_points();

        // Check for exit if in position
        if self.in_position
            && let Some(ref side) = self.entry_side
        {
            let exit = self.check_exit(close, ema_l, atr_val, side);
            if let Some(exit_signal) = exit {
                self.exit_position();
                return self.make_result(
                    exit_signal,
                    None,
                    None,
                    0.8,
                    format!("Pullback exit: price {:.2} (EMA_L={:.2})", close, ema_l),
                );
            }
        }

        // Already in position — don't open another
        if self.in_position {
            return self.make_result(
                TrendPullbackSignal::Hold,
                None,
                None,
                0.0,
                "In position".to_string(),
            );
        }

        // Need at least 2 candles for pattern detection
        if self.candles.len() < 2 {
            return self.make_result(
                TrendPullbackSignal::Hold,
                None,
                None,
                0.0,
                "Need more candles for pattern detection".to_string(),
            );
        }

        let current = &self.candles[self.candles.len() - 1];
        let prev = &self.candles[self.candles.len() - 2];

        // Need a valid swing range
        if swing_high <= swing_low || (swing_high - swing_low) < atr_val * 0.5 {
            return self.make_result(
                TrendPullbackSignal::Hold,
                None,
                None,
                0.0,
                format!(
                    "Swing range too narrow: high {:.2} low {:.2}",
                    swing_high, swing_low
                ),
            );
        }

        // === BULLISH PULLBACK ===
        if trend == Some(TrendDirection::Bullish) {
            // Check HTF agreement (allow if htf not set or bullish)
            let htf_ok = htf.is_none() || htf == Some(TrendDirection::Bullish);
            if !htf_ok {
                return self.make_result(
                    TrendPullbackSignal::Hold,
                    None,
                    None,
                    0.0,
                    "HTF trend disagrees with bullish pullback".to_string(),
                );
            }

            // Check if price is in a Fibonacci zone
            if let Some(fib_level) = self.check_fib_zone(close, swing_high, swing_low) {
                // RSI confirmation: approaching oversold
                let rsi_bullish = rsi_now
                    .map(|r| r < self.config.rsi_oversold + self.config.rsi_approach_offset)
                    .unwrap_or(false);

                // Candlestick reversal pattern
                let is_pin = current.is_bullish()
                    && current.is_bullish_pin_bar(self.config.pin_bar_wick_ratio);
                let is_engulf = current.is_bullish_engulfing(prev);
                let candle_signal = current.is_bullish() && (is_pin || is_engulf);

                if rsi_bullish && candle_signal {
                    let stop = swing_low - atr_val * self.config.atr_stop_multiplier;
                    let tp = swing_high + atr_val * self.config.atr_tp_multiplier;

                    let mut confidence = 0.40;

                    // Deeper pullback = more confidence (closer to 0.618 is better)
                    confidence += (1.0 - fib_level) * 0.20;

                    // RSI fully oversold
                    if let Some(r) = rsi_now
                        && r < self.config.rsi_oversold
                    {
                        confidence += 0.15;
                    }

                    // Engulfing is stronger than pin bar
                    if is_engulf {
                        confidence += 0.10;
                    } else if is_pin {
                        confidence += 0.05;
                    }

                    // HTF agreement
                    if htf == Some(TrendDirection::Bullish) {
                        confidence += 0.10;
                    }

                    confidence = confidence.min(1.0);

                    let pattern_name = if is_engulf { "engulfing" } else { "pin bar" };

                    if confidence >= self.config.min_confidence {
                        self.enter_position(TrendPullbackSignal::Buy, close, stop, tp);
                        self.last_signal = TrendPullbackSignal::Buy;

                        return self.make_result(
                            TrendPullbackSignal::Buy,
                            Some(fib_level),
                            Some(pattern_name.to_string()),
                            confidence,
                            format!(
                                "Bullish pullback to {:.1}% Fib, RSI {:.1}, {} pattern, swing [{:.2}, {:.2}]",
                                fib_level * 100.0,
                                rsi_now.unwrap_or(0.0),
                                pattern_name,
                                swing_low,
                                swing_high,
                            ),
                        );
                    }
                }
            }
        }

        // === BEARISH PULLBACK ===
        if trend == Some(TrendDirection::Bearish) {
            let htf_ok = htf.is_none() || htf == Some(TrendDirection::Bearish);
            if !htf_ok {
                return self.make_result(
                    TrendPullbackSignal::Hold,
                    None,
                    None,
                    0.0,
                    "HTF trend disagrees with bearish pullback".to_string(),
                );
            }

            // For bearish pullback, price rallies UP into the fib zone
            // (we invert: measure retracement from low to high)
            if let Some(fib_level) = self.check_fib_zone_inverted(close, swing_high, swing_low) {
                let rsi_bearish = rsi_now
                    .map(|r| r > self.config.rsi_overbought - self.config.rsi_approach_offset)
                    .unwrap_or(false);

                let is_pin = current.is_bearish()
                    && current.is_bearish_pin_bar(self.config.pin_bar_wick_ratio);
                let is_engulf = current.is_bearish_engulfing(prev);
                let candle_signal = current.is_bearish() && (is_pin || is_engulf);

                if rsi_bearish && candle_signal {
                    let stop = swing_high + atr_val * self.config.atr_stop_multiplier;
                    let tp = swing_low - atr_val * self.config.atr_tp_multiplier;

                    let mut confidence = 0.40;

                    confidence += fib_level * 0.20;

                    if let Some(r) = rsi_now
                        && r > self.config.rsi_overbought
                    {
                        confidence += 0.15;
                    }

                    if is_engulf {
                        confidence += 0.10;
                    } else if is_pin {
                        confidence += 0.05;
                    }

                    if htf == Some(TrendDirection::Bearish) {
                        confidence += 0.10;
                    }

                    confidence = confidence.min(1.0);

                    let pattern_name = if is_engulf { "engulfing" } else { "pin bar" };

                    if confidence >= self.config.min_confidence {
                        self.enter_position(TrendPullbackSignal::Sell, close, stop, tp);
                        self.last_signal = TrendPullbackSignal::Sell;

                        return self.make_result(
                            TrendPullbackSignal::Sell,
                            Some(fib_level),
                            Some(pattern_name.to_string()),
                            confidence,
                            format!(
                                "Bearish pullback to {:.1}% Fib, RSI {:.1}, {} pattern, swing [{:.2}, {:.2}]",
                                fib_level * 100.0,
                                rsi_now.unwrap_or(0.0),
                                pattern_name,
                                swing_low,
                                swing_high,
                            ),
                        );
                    }
                }
            }
        }

        self.last_signal = TrendPullbackSignal::Hold;
        self.make_result(
            TrendPullbackSignal::Hold,
            None,
            None,
            0.0,
            format!(
                "No signal: price {:.2}, trend {:?}, swing [{:.2}, {:.2}], RSI {:.1}",
                close,
                trend,
                swing_low,
                swing_high,
                rsi_now.unwrap_or(0.0),
            ),
        )
    }

    /// Update with HLC values (no volume).
    pub fn update_hlc(&mut self, high: f64, low: f64, close: f64) -> TrendPullbackResult {
        self.update_ohlcv(close, high, low, close, 0.0)
    }

    // ========================================================================
    // Fibonacci helpers
    // ========================================================================

    /// Calculate a Fibonacci retracement level.
    /// For a bullish swing (low → high), fib level 0.382 means price has
    /// retraced 38.2% from the high back toward the low.
    fn fib_price(high: f64, low: f64, level: f64) -> f64 {
        high - (high - low) * level
    }

    /// For a bearish swing (high → low), inverted fib.
    fn fib_price_inverted(high: f64, low: f64, level: f64) -> f64 {
        low + (high - low) * level
    }

    /// Check if price is in any of the configured Fibonacci zones
    /// (bullish retracement: price pulling back from high toward low).
    fn check_fib_zone(&self, price: f64, high: f64, low: f64) -> Option<f64> {
        let swing_range = high - low;
        if swing_range <= 0.0 {
            return None;
        }
        let tolerance = swing_range * self.config.fib_tolerance;

        for &level in &self.config.fib_levels {
            let fib_price = Self::fib_price(high, low, level);
            if (price - fib_price).abs() <= tolerance {
                return Some(level);
            }
        }
        None
    }

    /// Check if price is in any Fibonacci zone for bearish retracement
    /// (price rallying from low back toward high).
    fn check_fib_zone_inverted(&self, price: f64, high: f64, low: f64) -> Option<f64> {
        let swing_range = high - low;
        if swing_range <= 0.0 {
            return None;
        }
        let tolerance = swing_range * self.config.fib_tolerance;

        for &level in &self.config.fib_levels {
            let fib_price = Self::fib_price_inverted(high, low, level);
            if (price - fib_price).abs() <= tolerance {
                return Some(level);
            }
        }
        None
    }

    // ========================================================================
    // Swing point computation
    // ========================================================================

    /// Compute the swing high and swing low from the recent candle history.
    fn compute_swing_points(&self) -> (f64, f64) {
        let lookback = self.config.swing_lookback.min(self.candles.len());
        if lookback == 0 {
            return (0.0, f64::MAX);
        }

        let start = self.candles.len() - lookback;
        let mut high = f64::MIN;
        let mut low = f64::MAX;

        for candle in &self.candles[start..] {
            if candle.high > high {
                high = candle.high;
            }
            if candle.low < low {
                low = candle.low;
            }
        }

        (high, low)
    }

    // ========================================================================
    // Regime gating
    // ========================================================================

    /// Check whether this strategy should trade in the given market regime.
    ///
    /// Trend Pullback needs a clear trend to pull back within.
    pub fn should_trade_in_regime(regime: &MarketRegime) -> bool {
        matches!(regime, MarketRegime::Trending(_) | MarketRegime::Volatile)
    }

    /// Compute a confidence-weighted position size factor for the given regime.
    pub fn regime_size_factor(regime: &MarketRegime, confidence: f64) -> Option<f64> {
        match regime {
            MarketRegime::Trending(_) => {
                // Primary regime — full confidence scaling
                Some(confidence.clamp(0.4, 1.0))
            }
            MarketRegime::Volatile => {
                // Deeper pullbacks possible but riskier
                Some(if confidence >= 0.6 { 0.35 } else { 0.20 })
            }
            _ => None,
        }
    }

    // ========================================================================
    // Position management (internal)
    // ========================================================================

    fn enter_position(&mut self, side: TrendPullbackSignal, price: f64, stop: f64, tp: f64) {
        self.in_position = true;
        self.entry_price = Some(price);
        self.entry_side = Some(side);
        self.stop_loss = Some(stop);
        self.take_profit = Some(tp);
    }

    fn check_exit(
        &self,
        close: f64,
        ema_long: f64,
        _atr: f64,
        side: &TrendPullbackSignal,
    ) -> Option<TrendPullbackSignal> {
        match side {
            TrendPullbackSignal::Buy => {
                // Exit long if price closes below long EMA (trend break)
                if close < ema_long {
                    return Some(TrendPullbackSignal::Sell);
                }
                // Stop loss
                if let Some(stop) = self.stop_loss
                    && close <= stop
                {
                    return Some(TrendPullbackSignal::Sell);
                }
                // Take profit
                if let Some(tp) = self.take_profit
                    && close >= tp
                {
                    return Some(TrendPullbackSignal::Sell);
                }
                None
            }
            TrendPullbackSignal::Sell => {
                // Exit short if price closes above long EMA
                if close > ema_long {
                    return Some(TrendPullbackSignal::Buy);
                }
                if let Some(stop) = self.stop_loss
                    && close >= stop
                {
                    return Some(TrendPullbackSignal::Buy);
                }
                if let Some(tp) = self.take_profit
                    && close <= tp
                {
                    return Some(TrendPullbackSignal::Buy);
                }
                None
            }
            TrendPullbackSignal::Hold => None,
        }
    }

    fn exit_position(&mut self) {
        self.in_position = false;
        self.entry_price = None;
        self.entry_side = None;
        self.stop_loss = None;
        self.take_profit = None;
    }

    fn make_result(
        &self,
        signal: TrendPullbackSignal,
        fib_level_hit: Option<f64>,
        candle_pattern: Option<String>,
        confidence: f64,
        reason: String,
    ) -> TrendPullbackResult {
        let (swing_high, swing_low) = self.compute_swing_points();
        let trend = match (self.last_ema_short, self.last_ema_long) {
            (Some(s), Some(l)) if s > l => Some(TrendDirection::Bullish),
            (Some(s), Some(l)) if s < l => Some(TrendDirection::Bearish),
            _ => None,
        };

        TrendPullbackResult {
            signal,
            ema_short: self.last_ema_short,
            ema_long: self.last_ema_long,
            rsi: self.last_rsi,
            atr: self.last_atr,
            swing_high: if swing_high > f64::MIN {
                Some(swing_high)
            } else {
                None
            },
            swing_low: if swing_low < f64::MAX {
                Some(swing_low)
            } else {
                None
            },
            fib_level_hit,
            candle_pattern,
            trend,
            confidence,
            stop_loss: if matches!(signal, TrendPullbackSignal::Buy | TrendPullbackSignal::Sell) {
                self.stop_loss
            } else {
                None
            },
            take_profit: if matches!(signal, TrendPullbackSignal::Buy | TrendPullbackSignal::Sell) {
                self.take_profit
            } else {
                None
            },
            reason,
        }
    }

    // ========================================================================
    // Reset
    // ========================================================================

    /// Full reset — clears all indicator and position state.
    pub fn reset(&mut self) {
        self.ema_short = EMA::new(self.config.ema_short_period);
        self.ema_long = EMA::new(self.config.ema_long_period);
        self.rsi = RSI::new(self.config.rsi_period);
        self.atr = ATR::new(self.config.atr_period);
        self.candles.clear();
        self.exit_position();
        self.last_ema_short = None;
        self.last_ema_long = None;
        self.last_rsi = None;
        self.last_atr = None;
        self.last_signal = TrendPullbackSignal::Hold;
        self.candle_count = 0;
        self.htf_trend = None;
    }

    // ========================================================================
    // Accessors
    // ========================================================================

    /// Whether the strategy has enough data to produce signals.
    pub fn is_ready(&self) -> bool {
        self.candle_count >= self.config.warmup_candles
            && self.last_ema_short.is_some()
            && self.last_ema_long.is_some()
            && self.last_atr.is_some()
    }

    /// Whether the strategy currently has an open position.
    pub fn is_in_position(&self) -> bool {
        self.in_position
    }

    /// The last short EMA value.
    pub fn last_ema_short(&self) -> Option<f64> {
        self.last_ema_short
    }

    /// The last long EMA value.
    pub fn last_ema_long(&self) -> Option<f64> {
        self.last_ema_long
    }

    /// The last RSI value.
    pub fn last_rsi(&self) -> Option<f64> {
        self.last_rsi
    }

    /// The last ATR value.
    pub fn last_atr(&self) -> Option<f64> {
        self.last_atr
    }

    /// The last signal produced.
    pub fn last_signal(&self) -> TrendPullbackSignal {
        self.last_signal
    }

    /// Total candles processed.
    pub fn candle_count(&self) -> usize {
        self.candle_count
    }

    /// Current entry price (if in position).
    pub fn entry_price(&self) -> Option<f64> {
        self.entry_price
    }

    /// Current stop loss (if in position).
    pub fn stop_loss(&self) -> Option<f64> {
        self.stop_loss
    }

    /// Current take profit (if in position).
    pub fn take_profit(&self) -> Option<f64> {
        self.take_profit
    }

    /// Strategy configuration.
    pub fn config(&self) -> &TrendPullbackConfig {
        &self.config
    }

    /// Current HTF trend bias (if set).
    pub fn htf_trend(&self) -> Option<TrendDirection> {
        self.htf_trend
    }

    /// Current swing high/low from candle history.
    pub fn swing_points(&self) -> (f64, f64) {
        self.compute_swing_points()
    }

    /// Detected trend direction from EMAs.
    pub fn detected_trend(&self) -> Option<TrendDirection> {
        match (self.last_ema_short, self.last_ema_long) {
            (Some(s), Some(l)) if s > l => Some(TrendDirection::Bullish),
            (Some(s), Some(l)) if s < l => Some(TrendDirection::Bearish),
            _ => None,
        }
    }
}

