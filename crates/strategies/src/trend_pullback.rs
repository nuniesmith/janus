/// Signal generated by the Trend Pullback strategy.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Serialize, Deserialize)]
pub enum TrendPullbackSignal {
    /// Enter long — bullish pullback to Fibonacci zone with reversal pattern.
    Buy,
    /// Enter short / exit long — bearish pullback with reversal pattern.
    Sell,
    /// No action.
    Hold,
}

impl std::fmt::Display for TrendPullbackSignal {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            TrendPullbackSignal::Buy => write!(f, "BUY"),
            TrendPullbackSignal::Sell => write!(f, "SELL"),
            TrendPullbackSignal::Hold => write!(f, "HOLD"),
        }
    }
}

