/// Minimal candle data stored for swing point and pattern detection.
#[derive(Debug, Clone)]
struct CandleData {
    open: f64,
    high: f64,
    low: f64,
    close: f64,
}

#[allow(dead_code)]
impl CandleData {
    fn is_bullish(&self) -> bool {
        self.close > self.open
    }

    fn is_bearish(&self) -> bool {
        self.close < self.open
    }

    fn body_size(&self) -> f64 {
        (self.close - self.open).abs()
    }

    fn range(&self) -> f64 {
        self.high - self.low
    }

    fn upper_wick(&self) -> f64 {
        self.high - self.close.max(self.open)
    }

    fn lower_wick(&self) -> f64 {
        self.close.min(self.open) - self.low
    }

    /// Check if this candle is a pin bar (long wick rejection).
    fn is_pin_bar(&self, wick_ratio: f64) -> bool {
        let body = self.body_size();
        let range = self.range();
        if range <= 0.0 || body <= 0.0 {
            return false;
        }
        // Bullish pin bar: long lower wick
        let bullish_pin = self.lower_wick() >= body * wick_ratio && self.upper_wick() < body;
        // Bearish pin bar: long upper wick
        let bearish_pin = self.upper_wick() >= body * wick_ratio && self.lower_wick() < body;

        bullish_pin || bearish_pin
    }

    /// Check if this candle is a bullish pin bar (hammer).
    fn is_bullish_pin_bar(&self, wick_ratio: f64) -> bool {
        let body = self.body_size();
        if body <= 0.0 {
            return false;
        }
        self.lower_wick() >= body * wick_ratio && self.upper_wick() < body
    }

    /// Check if this candle is a bearish pin bar (shooting star).
    fn is_bearish_pin_bar(&self, wick_ratio: f64) -> bool {
        let body = self.body_size();
        if body <= 0.0 {
            return false;
        }
        self.upper_wick() >= body * wick_ratio && self.lower_wick() < body
    }

    /// Check if this candle engulfs the previous candle.
    fn is_engulfing(&self, prev: &CandleData) -> bool {
        if self.is_bullish() && prev.is_bearish() {
            // Bullish engulfing
            self.close > prev.open && self.open < prev.close
        } else if self.is_bearish() && prev.is_bullish() {
            // Bearish engulfing
            self.close < prev.open && self.open > prev.close
        } else {
            false
        }
    }

    /// Check if this is a bullish engulfing of the previous candle.
    fn is_bullish_engulfing(&self, prev: &CandleData) -> bool {
        self.is_bullish() && prev.is_bearish() && self.close > prev.open && self.open < prev.close
    }

    /// Check if this is a bearish engulfing of the previous candle.
    fn is_bearish_engulfing(&self, prev: &CandleData) -> bool {
        self.is_bearish() && prev.is_bullish() && self.close < prev.open && self.open > prev.close
    }
}

