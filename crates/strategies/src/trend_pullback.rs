//! # Trend Pullback Strategy
//!
//! Identifies pullbacks within established trends using Fibonacci retracement
//! levels, RSI divergence, and candlestick reversal patterns (pin bars,
//! engulfing candles).
//!
//! ## How It Works
//!
//! 1. **Trend Identification**: EMA 50/200 determines the primary trend
//!    direction. Both EMAs must agree for a valid setup.
//!
//! 2. **Swing Point Tracking**: Maintains rolling swing high/low over recent
//!    candles to define the retracement range.
//!
//! 3. **Fibonacci Zone**: Price must pull back into a configurable Fibonacci
//!    retracement zone (default: 38.2%, 50.0%, 61.8%) of the last swing.
//!
//! 4. **RSI Confirmation**: RSI should be approaching oversold (for longs) or
//!    overbought (for shorts), indicating the pullback is exhausting.
//!
//! 5. **Candlestick Reversal**: The entry candle must show a reversal pattern
//!    — either a pin bar (long wick rejection) or an engulfing pattern.
//!
//! 6. **ATR-Based Stops**: Stop loss below/above the swing low/high, take
//!    profit at the swing high/low extended by an ATR multiple.
//!
//! ## Regime Integration
//!
//! This strategy is designed for trending markets with pullback opportunities:
//!
//! - **Trending(Bullish/Bearish)** → Yes (primary use case)
//! - **Volatile** → Yes with reduced size (deeper pullbacks possible)
//! - **MeanReverting** → No (no clear trend to pull back within)
//! - **Uncertain** → No (wait for clarity)
//!
//! ## Origin
//!
//! Ported from Kraken's `strategy/intraday.rs::TrendPullback`, adapted for
//! the JANUS single-asset design, `janus-regime` indicator types, and
//! regime-aware position sizing.

use janus_regime::MarketRegime;
use janus_regime::indicators::{ATR, EMA, RSI};
use janus_regime::types::TrendDirection;
use serde::{Deserialize, Serialize};

